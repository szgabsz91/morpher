package com.github.szgabsz91.morpher.methods.tasr.impl.tree;

import com.github.szgabsz91.morpher.methods.tasr.impl.rules.SuffixRule;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.util.Collection;
import java.util.HashMap;
import java.util.HashSet;
import java.util.Map;
import java.util.Optional;
import java.util.Set;

import static java.util.Comparator.comparingInt;

/**
 * This class represents a node in the TASR tree.
 *
 * <p>The node contains a suffix rule (may be null) and the child nodes.</p>
 *
 * @author szgabsz91
 */
public class TASRTreeNode {

    private static final Logger LOGGER = LoggerFactory.getLogger(TASRTreeNode.class);

    private final int id;
    private final char firstCharacter;
    private final Set<SuffixRule> suffixRules;
    private TASRTreeNode parent;
    private final Map<Character, TASRTreeNode> children;

    /**
     * Constructor that adds this object to the parent node if the given parent node is not null and initializes all
     * the properties.
     *
     * @param id the unique identifier in the tree
     * @param firstCharacter the first character of the suffix rules that can be stored in this tree node
     * @param parent the parent tree node
     */
    public TASRTreeNode(final int id, final char firstCharacter, final TASRTreeNode parent) {
        this.id = id;
        this.firstCharacter = firstCharacter;
        this.suffixRules = new HashSet<>();
        this.parent = parent;
        this.children = new HashMap<>();

        if (parent != null) {
            this.setParent(parent);
        }
    }

    /**
     * Creates a new root node.
     * @param id the unique identifier
     * @return the root node
     */
    public static TASRTreeNode root(final int id) {
        return new TASRTreeNode(id, '\0', null);
    }

    /**
     * Returns the unique identifier of the tree node.
     * @return the unique identifier of the tree node
     */
    public int getId() {
        return id;
    }

    /**
     * Returns the first character of the suffix rules in this tree node.
     * @return the first character of the suffix rules in this tree node
     */
    public char getFirstCharacter() {
        return firstCharacter;
    }

    /**
     * Returns the set of stored suffix rules.
     * @return the set of stored suffix rules
     */
    public Set<SuffixRule> getSuffixRules() {
        return suffixRules;
    }

    /**
     * Returns the optional winning suffix rule stored in the tree node, with the highest frequency.
     * @return the winning suffix rule of the tree node inside an optional
     */
    public Optional<SuffixRule> getWinningSuffixRule() {
        return this.suffixRules
                .stream()
                .max(comparingInt(SuffixRule::getFrequency));
    }

    /**
     * Adds the given suffix rule to the tree node if it is not already present.
     *
     * <p>Otherwise the existing suffix rule's frequency is incremented by one.</p>
     *
     * @param suffixRule the new suffix rule
     */
    public void addSuffixRule(final SuffixRule suffixRule) {
        final String leftHandSuffix = suffixRule.getLeftHandSuffix();

        if (!leftHandSuffix.isEmpty()) {
            final char firstCharacter = leftHandSuffix.charAt(0);

            if (firstCharacter != this.firstCharacter) {
                throw new IllegalArgumentException(
                        suffixRule + " starts with " + firstCharacter + " instead of " + this.firstCharacter
                );
            }
        }

        this.suffixRules
                .stream()
                .filter(suffixRule::equals)
                .findFirst()
                .ifPresentOrElse(SuffixRule::incrementFrequency, () -> this.suffixRules.add(suffixRule));
    }

    /**
     * Returns the parent node.
     * @return the parent node
     */
    public TASRTreeNode getParent() {
        return parent;
    }

    /**
     * Sets the parent of the tree node.
     * @param parent the parent of the tree node
     */
    public final void setParent(final TASRTreeNode parent) {
        if (this.parent != null) {
            this.parent.children.remove(this.firstCharacter);
        }

        this.parent = parent;

        parent.getChild(this.firstCharacter)
                .ifPresentOrElse(this::noop, () -> parent.addChild(firstCharacter, this));
    }

    /**
     * Returns the children of the tree node.
     * @return the children of the tree node
     */
    public Collection<TASRTreeNode> getChildren() {
        return children.values();
    }

    /**
     * Returns the optional child whose suffix rules have a left-hand suffix starting with the given character.
     * @param character the character
     * @return the optional child
     */
    public Optional<TASRTreeNode> getChild(final char character) {
        return Optional.ofNullable(this.children.get(character));
    }

    /**
     * Adds the given child for the given character.
     * @param character the character
     * @param treeNode the new child
     */
    public final void addChild(final char character, final TASRTreeNode treeNode) {
        if (this.children.containsKey(character)) {
            throw new IllegalArgumentException("The given character already has an associated child node");
        }

        this.children.put(character, treeNode);

        if (treeNode.getParent() != this) {
            treeNode.setParent(this);
        }
    }

    /**
     * Returns the number of nodes under this node, including this node and all its descendants.
     * @return the number of nodes in the subtree
     */
    public int calculateSizeOfSubtree() {
        final int sizeOfSubtree = this.children
                .values()
                .stream()
                .mapToInt(TASRTreeNode::calculateSizeOfSubtree)
                .sum();
        return sizeOfSubtree + 1;
    }

    /**
     * Traverses the subtree under this tree node in a post-order way.
     *
     * <p>For every tree node, the given traverser is executed that can return a suffix rule or null.</p>
     *
     * @param treeTraverser the traverser function
     * @return the first non-null suffix rule returned by the given traverser
     */
    Optional<TASRTreeNode> traverse(final ITASRTreeTraverser treeTraverser) {
        for (final TASRTreeNode child : children.values()) {
            final Optional<TASRTreeNode> optionalResult = child.traverse(treeTraverser);

            if (optionalResult.isPresent()) {
                LOGGER.trace("Tree node returned from subtree: {}", optionalResult.get());
                return optionalResult;
            }
        }

        final Optional<TASRTreeNode> result = treeTraverser.traverse(this);
        LOGGER.trace("Resulting tree node: {}", result);
        return result;
    }

    private void noop(final TASRTreeNode treeNode) {

    }

    /**
     * Returns if this tree node equals the given other object.
     *
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object other) {
        if (this == other) {
            return true;
        }
        if (other == null || getClass() != other.getClass()) {
            return false;
        }
        final TASRTreeNode that = (TASRTreeNode) other;
        return id == that.id;
    }

    /**
     * Returns the hash code of this tree node.
     *
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        return id;
    }

    /**
     * Returns the string representation of this tree node.
     *
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        return "TASRTreeNode[id=" + id + ", suffixRules=" + suffixRules + "]";
    }

}
