package com.github.szgabsz91.morpher.methods.fst.impl;

import java.util.ArrayList;
import java.util.Collection;
import java.util.Comparator;
import java.util.HashSet;
import java.util.List;

/**
 * List implementation that does not allow duplicate elements, and keeps the order of items based on a
 * {@link Comparator}.
 *
 * @author szgabsz91
 * @param <T> the type of items in the list
 */
public class UniqueSortedList<T> extends ArrayList<T> {

    private static final long serialVersionUID = -6735256122005238100L;

    private final Comparator<? super T> comparator;

    /**
     * Constructor that sets the comparator.
     * @param comparator the comparator
     */
    public UniqueSortedList(final Comparator<? super T> comparator) {
        this.comparator = comparator;
    }

    /**
     * Searches for the two neighbors of the given element and inserts it into the appropriate position.
     * @param element the element to add to the list
     * @return true if the element is inserted, false otherwise
     */
    @Override
    public boolean add(final T element) {
        if (this.isEmpty()) {
            return super.add(element);
        }

        int index = 0;

        while (index < this.size() && this.comparator.compare(this.get(index), element) < 0) {
            index++;
        }

        if (index >= this.size()) {
            return super.add(element);
        }

        if (this.comparator.compare(this.get(index), element) == 0) {
            return false;
        }

        super.add(index, element);
        return true;
    }

    /**
     * Drops the duplicate elements from the given collection and sorts it.
     *
     * <p>Each element is inserted into the appropriate position, and if the element is duplicate, it is dropped.</p>
     *
     * @param collection the collection whose elements should be added
     * @return true if at least one element is added, false otherwise
     */
    @Override
    public boolean addAll(final Collection<? extends T> collection) {
        boolean result = false;
        final List<T> list = new ArrayList<>(new HashSet<>(collection));
        list.sort(this.comparator);

        if (this.isEmpty()) {
            return super.addAll(list);
        }

        int index = 0;

        while (!list.isEmpty()) {
            final T element = list.remove(0);

            while (index < this.size() && this.comparator.compare(this.get(index), element) < 0) {
                index++;
            }

            if (index >= this.size()) {
                list.add(0, element);
                return super.addAll(list);
            }

            if (this.comparator.compare(this.get(index), element) == 0) {
                continue;
            }

            super.add(index, element);
            result = true;
        }

        return result;
    }

    /**
     * Adding an element to a specified index is not supported by this implementation.
     * @param index the index of the element
     * @param element the element to add
     */
    @Override
    public void add(final int index, final T element) {
        throw new UnsupportedOperationException("You cannot directly add an item to an index");
    }

    /**
     * Adding a collection to a specified index is not supported by this implementation.
     * @param index the index of the first element
     * @param collection the collection to add
     * @return true if at least one element is added, false otherwise
     */
    @Override
    public boolean addAll(final int index, final Collection<? extends T> collection) {
        throw new UnsupportedOperationException("You cannot directly set add items to an index");
    }

    /**
     * Setting an element at a specified index is not supported by this implementation.
     * @param index index of the element to replace
     * @param element element to be stored at the specified position
     * @return the element previously at the specified position
     */
    @Override
    public T set(final int index, final T element) {
        throw new UnsupportedOperationException("You cannot directly set an item");
    }

    /**
     * Returns if this sorted list is equal to the given other object.
     *
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object other) {
        return super.equals(other);
    }

    /**
     * Returns the hash code of this object.
     *
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        return super.hashCode();
    }

}
