import java.util.Collection;
import java.util.HashSet;
import java.util.Iterator;
import java.util.List;
import java.util.ListIterator;
import java.util.Set;

import static java.util.stream.Collectors.toSet;

/**
 * Calculates if one object is subset of another one.
 *
 * @author szgabsz91
 */
public final class SubsetCalculator {

    private SubsetCalculator() {

    }

    /**
     * Returns if the first character is a subset of the second one.
     * @param character1 the first {@link ICharacter} object
     * @param character2 the second {@link ICharacter} object
     * @return true if the first character is a subset of the second one, false otherwise
     */
    public static boolean isSubsetOf(final ICharacter character1, final ICharacter character2) {
        if (!character1.getClass().equals(character2.getClass())) {
            return false;
        }

        if (character1 instanceof Character) {
            return character1.getAttributes().iterator().next().equals(character2.getAttributes().iterator().next());
        }

        if (character1 instanceof Vowel) {
            final Vowel vowel1 = (Vowel) character1;
            final Vowel vowel2 = (Vowel) character2;
            @SuppressWarnings("unchecked")
            final Collection<? extends IVowelAttribute> vowelAttributes1 =
                    (Collection<? extends IVowelAttribute>) vowel1.getAttributes();
            @SuppressWarnings("unchecked")
            final Collection<? extends IVowelAttribute> vowelAttributes2 =
                    (Collection<? extends IVowelAttribute>) vowel2.getAttributes();
            return isSubsetAttributes(vowelAttributes1, vowelAttributes2, vowel1, vowel2);
        }

        if (character1 instanceof Consonant) {
            final Consonant consonant1 = (Consonant) character1;
            final Consonant consonant2 = (Consonant) character2;
            @SuppressWarnings("unchecked")
            final Collection<? extends IConsonantAttribute> consonantAttributes1 =
                    (Collection<? extends IConsonantAttribute>) consonant1.getAttributes();
            @SuppressWarnings("unchecked")
            final Collection<? extends IConsonantAttribute> consonantAttributes2 =
                    (Collection<? extends IConsonantAttribute>) consonant2.getAttributes();
            return isSubsetAttributes(consonantAttributes1, consonantAttributes2, consonant1, consonant2);
        }

        if (character1 instanceof StartSound || character1 instanceof EndSound) {
            return true;
        }

        throw new IllegalArgumentException("Uncovered character types found: " + character1 + " and " + character2);
    }

    private static boolean isSubsetAttributes(
            final Collection<? extends IVowelAttribute> attributes1,
            final Collection<? extends IVowelAttribute> attributes2,
            final Vowel vowel1,
            final Vowel vowel2) {
        final Set<? extends Class<?>> attributeClasses1 = attributes1
                .stream()
                .map(Object::getClass)
                .collect(toSet());
        final Set<? extends Class<?>> attributeClasses2 = attributes2
                .stream()
                .map(Object::getClass)
                .collect(toSet());

        if (!attributeClasses2.containsAll(attributeClasses1)) {
            return false;
        }

        final Set<? extends Class<?>> commonAttributeClasses = new HashSet<>(attributeClasses1);
        commonAttributeClasses.retainAll(attributeClasses2);

        for (final Class<?> commonAttributeClass : commonAttributeClasses) {
            @SuppressWarnings("unchecked")
            final IVowelAttribute attribute1 = vowel1.get((Class<? extends IAttribute>) commonAttributeClass);
            @SuppressWarnings("unchecked")
            final IVowelAttribute attribute2 = vowel2.get((Class<? extends IAttribute>) commonAttributeClass);

            if (!attribute1.equals(attribute2)) {
                return false;
            }
        }

        return true;
    }

    private static boolean isSubsetAttributes(
            final Collection<? extends IConsonantAttribute> attributes1,
            final Collection<? extends IConsonantAttribute> attributes2,
            final Consonant consonant1,
            final Consonant consonant2) {
        final Set<? extends Class<?>> attributeClasses1 = attributes1
                .stream()
                .map(Object::getClass)
                .collect(toSet());
        final Set<? extends Class<?>> attributeClasses2 = attributes2
                .stream()
                .map(Object::getClass)
                .collect(toSet());

        if (!attributeClasses2.containsAll(attributeClasses1)) {
            return false;
        }

        final Set<? extends Class<?>> commonAttributeClasses = new HashSet<>(attributeClasses1);
        commonAttributeClasses.retainAll(attributeClasses2);

        for (final Class<?> commonAttributeClass : commonAttributeClasses) {
            @SuppressWarnings("unchecked")
            final IConsonantAttribute attribute1 = consonant1.get((Class<? extends IAttribute>) commonAttributeClass);
            @SuppressWarnings("unchecked")
            final IConsonantAttribute attribute2 = consonant2.get((Class<? extends IAttribute>) commonAttributeClass);

            if (!attribute1.equals(attribute2)) {
                return false;
            }
        }

        return true;
    }

    static boolean isSubsetOf(final ITransformation transformation1, final ITransformation transformation2) {
        if (!transformation1.getClass().equals(transformation2.getClass())) {
            return false;
        }

        if (transformation1 instanceof Addition) {
            return isSubsetOf((Addition) transformation1, (Addition) transformation2);
        }

        if (transformation1 instanceof Removal) {
            return isSubsetOf((Removal) transformation1, (Removal) transformation2);
        }

        if (transformation1 instanceof Replacement) {
            return isSubsetOf((Replacement) transformation1, (Replacement) transformation2);
        }

        throw new IllegalArgumentException(
                "Unknown transformation types found: " + transformation1 + " and " + transformation2
        );
    }

    private static boolean isSubsetOf(final Addition addition1, final Addition addition2) {
        return addition2.getAttributes().containsAll(addition1.getAttributes());
    }

    private static boolean isSubsetOf(final Removal removal1, final Removal removal2) {
        return removal2.getAttributes().containsAll(removal1.getAttributes());
    }

    static boolean isSubsetOf(
            final AttributeDelta<? extends IAttribute> attributeDelta1,
            final AttributeDelta<? extends IAttribute> attributeDelta2) {
        return attributeDelta1.getClazz().equals(attributeDelta2.getClazz()) &&
                attributeDelta1.getFrom().equals(attributeDelta2.getFrom()) &&
                attributeDelta1.getTo().equals(attributeDelta2.getTo());
    }

    private static boolean isSubsetOf(final Replacement replacement1, final Replacement replacement2) {
        final AttributeDeltaSetHolder attributeDeltaSetHolder = new AttributeDeltaSetHolder(
                replacement1.getAttributeDeltas(),
                replacement2.getAttributeDeltas()
        );

        final boolean isAttributeSetNonCommon = attributeDeltaSetHolder.getAttributeDeltaClasses2().containsAll(
                attributeDeltaSetHolder.getAttributeDeltaClasses1()
        );

        if (!isAttributeSetNonCommon) {
            return false;
        }

        for (final Class<? super IAttribute> clazz : attributeDeltaSetHolder.getCommonAttributeDeltaClasses()) {
            final AttributeDelta<? super IAttribute> attributeDelta1 =
                    attributeDeltaSetHolder.getAttributeDelta1(clazz);
            final AttributeDelta<? super IAttribute> attributeDelta2 =
                    attributeDeltaSetHolder.getAttributeDelta2(clazz);
            final boolean isAttributeDeltaSubset = isSubsetOf(attributeDelta1, attributeDelta2);

            if (!isAttributeDeltaSubset) {
                return false;
            }
        }

        return true;
    }

    static boolean isSubsetOf(final Context context1, final Context context2) {
        final boolean isPrefixSubset = isSubsetOfFromEnd(context1.getPrefix(), context2.getPrefix());
        if (!isPrefixSubset) {
            return false;
        }

        final boolean isCoreSubset = isSubsetOfFull(context1.getCore(), context2.getCore());
        if (!isCoreSubset) {
            return false;
        }

        final boolean isPostfixSubset = isSubsetOfFromStart(context1.getPostfix(), context2.getPostfix());
        return isPostfixSubset && isSubsetOfPosition(context1, context2);
    }

    private static boolean isSubsetOfFromStart(final List<ICharacter> list1, final List<ICharacter> list2) {
        final Iterator<ICharacter> iterator1 = list1.iterator();
        final Iterator<ICharacter> iterator2 = list2.iterator();

        while (iterator1.hasNext() && iterator2.hasNext()) {
            final ICharacter item1 = iterator1.next();
            final ICharacter item2 = iterator2.next();
            final boolean isItemSubset = isSubsetOf(item1, item2);

            if (!isItemSubset) {
                return false;
            }
        }

        return !iterator1.hasNext();
    }

    private static boolean isSubsetOfFromEnd(final List<ICharacter> list1, final List<ICharacter> list2) {
        final ListIterator<ICharacter> iterator1 = list1.listIterator(list1.size());
        final ListIterator<ICharacter> iterator2 = list2.listIterator(list2.size());

        while (iterator1.hasPrevious() && iterator2.hasPrevious()) {
            final ICharacter item1 = iterator1.previous();
            final ICharacter item2 = iterator2.previous();
            final boolean isItemSubset = isSubsetOf(item1, item2);

            if (!isItemSubset) {
                return false;
            }
        }

        return !iterator1.hasPrevious();
    }

    private static boolean isSubsetOfFull(final List<ICharacter> list1, final List<ICharacter> list2) {
        if (list1.size() != list2.size()) {
            return false;
        }

        for (int i = 0; i < list1.size(); i++) {
            final ICharacter item1 = list1.get(i);
            final ICharacter item2 = list2.get(i);
            final boolean isItemSubset = isSubsetOf(item1, item2);

            if (!isItemSubset) {
                return false;
            }
        }

        return true;
    }

    static boolean isSubsetOfPosition(final Context context1, final Context context2) {
        if (context1.getFrontPosition() != null) {
            if (context2.getFrontPosition() == null) {
                return false;
            }
            else {
                final boolean isFrontPositionSubset = isSubsetOf(
                        context1.getFrontPosition(),
                        context2.getFrontPosition()
                );
                if (!isFrontPositionSubset) {
                    return false;
                }
            }
        }

        if (context1.getBackPosition() != null) {
            if (context2.getBackPosition() == null) {
                return false;
            }
            else {
                final boolean isBackPositionSubset = isSubsetOf(context1.getBackPosition(), context2.getBackPosition());
                if (!isBackPositionSubset) {
                    return false;
                }
            }
        }

        return true;
    }

    static boolean isSubsetOf(final Position position1, final Position position2) {
        return position1.getIndex() == position2.getIndex();
    }

    /**
     * Returns if the first node is a subset of the second one.
     * @param node1 the first {@link Node} object
     * @param node2 the second {@link Node} object
     * @return true if the first node is a subset of the second one, false otherwise
     */
    public static boolean isSubsetOf(final Node node1, final Node node2) {
        if (node1 instanceof FullNode) {
            return true;
        }

        if (node1 instanceof EmptyNode) {
            return node2 instanceof EmptyNode;
        }

        if (node2 instanceof FullNode) {
            return false;
        }

        if (node2 instanceof EmptyNode) {
            return true;
        }

        final boolean isSubsetOfPosition =
                isSubsetOfPosition(node1.getRule().getContext(), node2.getRule().getContext());

        return isSubsetOfPosition && isPatternSubsetOf(node1.getPattern(), node2.getPattern());
    }

    private static boolean isPatternSubsetOf(final List<ICharacter> thisPattern, final List<ICharacter> otherPattern) {
        if (thisPattern.size() > otherPattern.size()) {
            return false;
        }

        for (int index = 0; index <= otherPattern.size() - thisPattern.size(); index++) {
            final List<ICharacter> otherSubPattern = otherPattern.subList(index, index + thisPattern.size());
            boolean isSubset = true;

            for (int subIndex = 0; subIndex < thisPattern.size(); subIndex++) {
                final ICharacter character1 = thisPattern.get(subIndex);
                final ICharacter character2 = otherSubPattern.get(subIndex);

                if (!isSubsetOf(character1, character2)) {
                    isSubset = false;
                    break;
                }
            }

            if (isSubset) {
                return true;
            }
        }

        return false;
    }

}
