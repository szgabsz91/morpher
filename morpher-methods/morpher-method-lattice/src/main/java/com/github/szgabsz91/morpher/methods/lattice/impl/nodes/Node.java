package com.github.szgabsz91.morpher.methods.lattice.impl.nodes;

import com.github.szgabsz91.morpher.methods.api.characters.ICharacter;
import com.github.szgabsz91.morpher.methods.api.characters.attributes.IAttribute;
import com.github.szgabsz91.morpher.methods.lattice.impl.ProcessingType;
import com.github.szgabsz91.morpher.methods.lattice.impl.rules.Context;
import com.github.szgabsz91.morpher.methods.lattice.impl.rules.Rule;
import com.github.szgabsz91.morpher.methods.lattice.impl.setoperations.SubsetCalculator;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.HashSet;
import java.util.List;
import java.util.Map;
import java.util.Objects;
import java.util.Set;

/**
 * Node class of the {@link com.github.szgabsz91.morpher.methods.lattice.impl.Lattice} class.
 *
 * <p>Since the node appears in a lattice and not in a tree, each node can have multiple parents and children. Also, a
 * node stores a {@link Rule} and has a computed property that is the pattern, i.e. the part of a word that needs to be
 * transformed by the stored rule. The pattern has a prefix, core and postfix part that get concatenated together from
 * the rule itself.</p>
 *
 * <p>Different status fields are also stored directly in the node so that when a
 * {@link com.github.szgabsz91.morpher.methods.lattice.impl.LatticeWalker} traverses through a lattice, each
 * node gets only processed once. So when a node is processed, this long field gets updated to the current value and
 * nodes only get processed if their status is less than the value in the current processing. Note that each
 * processing has a unique status ID, this way two processing will have different status.</p>
 *
 * <p>A node also has a frequency which denotes the number of rule atoms that support it, and a level that denotes
 * the length of route from the topmost node to this node.</p>
 *
 * @author szgabsz91
 */
public class Node {

    /** The rule stored in the node. */
    protected Rule rule;
    /** The parents of the node. */
    protected final List<Node> parents;
    /** The children of the node. */
    protected final List<Node> children;
    /** Flag that indicates if the node is inhomogeneous or not. */
    protected boolean inhomogeneous;
    private final Map<ProcessingType, Long> processingStatuses;
    private List<ICharacter> _pattern;

    /**
     * This is the maximum level of the node from the root. If the node is on multiple pathes from the root to the leaf,
     * the maximum value is stored here.
     */
    private long level;

    /**
     * This is the frequency of a node that gets incremented if a node is inserted below the node.
     * Initially frequency is 0.
     */
    private long frequency;

    /**
     * Constructor that sets the stored rule, the inhomogeneous flag, the list of parents and list of children, as well
     * as initializes the processing statuses.
     *
     * @param rule the rule
     * @param inhomogeneous flag that indicates if the node is inhomogeneous or not
     * @param parents the parents of the node
     * @param children the children of the node
     */
    public Node(final Rule rule, final boolean inhomogeneous, final List<Node> parents, final List<Node> children) {
        this.rule = rule;
        this.inhomogeneous = inhomogeneous;
        this.parents = parents;
        this.children = children;
        this.frequency = 0;
        this.processingStatuses = new HashMap<>();
    }

    /**
     * Constructor that sets the stored rule, the list of parents and list of children, as well as initializes the
     * processing statuses.
     *
     * <p>The node will be homogeneous by default.</p>
     *
     * @param rule the rule
     * @param parents the parents of the node
     * @param children the children of the node
     */
    public Node(final Rule rule, final List<Node> parents, final List<Node> children) {
        this(rule, false, parents, children);
    }

    /**
     * Constructor that sets the stored rule and initializes the processing statuses.
     *
     * <p>The lists of parents and children remain empty.</p>
     *
     * @param rule the rule
     * @param inhomogeneous flag that indicates if the node is inhomogeneous or not
     */
    public Node(final Rule rule, final boolean inhomogeneous) {
        this(rule, inhomogeneous, new ArrayList<>(), new ArrayList<>());
    }

    /**
     * Constructor that sets the stored rule and initializes the processing statuses.
     *
     * <p>The lists of parents and children remain empty and the node will be homogeneous by default.</p>
     *
     * @param rule the rule
     */
    public Node(final Rule rule) {
        this(rule, false);
    }

    /**
     * Returns the rule.
     * @return the rule
     */
    public Rule getRule() {
        return rule;
    }

    /**
     * Sets the rule.
     * @param rule the rule
     */
    public void setRule(final Rule rule) {
        this.rule = rule;
    }

    /**
     * Returns the list of parents.
     * @return the list of parents
     */
    public List<Node> getParents() {
        return parents;
    }

    /**
     * Returns the list of children.
     * @return the list of children
     */
    public List<Node> getChildren() {
        return children;
    }

    /**
     * Adds the given node as a parent to the node.
     *
     * <p>If the node is already a parent, nothing happens. Otherwise, the new parent will get this object as a new
     * child as well.</p>
     *
     * @param parent the new parent
     */
    public void addParent(final Node parent) {
        if (parents.contains(parent)) {
            return;
        }

        parents.add(parent);
        parent.addChild(this);
    }

    /**
     * Removes the given set of parent nodes.
     * @param parents the parents to remove
     */
    public void removeParents(final Set<Node> parents) {
        this.parents.removeAll(parents);
    }

    /**
     * Adds the given node as a child to the node.
     *
     * <p>If the node is already a child, nothing happens. Otherwise, the new child will get this object as a new
     * parent as well.</p>
     *
     * @param child the new child
     */
    public void addChild(final Node child) {
        if (children.contains(child)) {
            return;
        }

        children.add(child);
        child.addParent(this);
    }

    /**
     * Removes the given set of child nodes.
     * @param children the children to remove
     */
    public void removeChildren(final Set<Node> children) {
        this.children.removeAll(children);
    }

    /**
     * Returns whether this node is the full (i.e. topmost, root) node of the lattice.
     * @return true if this is the root node, false otherwise
     */
    public boolean isFull() {
        return false;
    }

    /**
     * Returns whether this node is the empty (i.e. lowest, leaf) node of the lattice.
     * @return true if this is the leaf node, false otherwise
     */
    public boolean isEmpty() {
        return false;
    }

    /**
     * Returns whether this node is homogeneous, i.e. it has a rule and the stored rule itself is homogeneous as well.
     * @return true if this is a homogeneous node, false otherwise
     */
    public boolean isHomogeneous() {
        return !this.isInhomogeneous();
    }

    /**
     * Returns whether this node is inhomogeneous, i.e. it doesn't have a rule or the stored rule itself is
     * inhomogeneous as well.
     *
     * @return true if this is an inhomogeneous node, false otherwise
     */
    public boolean isInhomogeneous() {
        return inhomogeneous;
    }

    /**
     * Sets the node to inhomogeneous.
     *
     * <p>After this call, {@link Node#isInhomogeneous()} will return <code>true</code> and
     * {@link Node#isHomogeneous()} will return <code>false</code>.</p>
     */
    public void setInhomogeneous() {
        this.inhomogeneous = true;

        if (this.rule != null) {
            this.rule.setTransformations(null);
        }
    }

    /**
     * Sets the inhomogeneous flag to the given value.
     * @param inhomogeneous the new value
     */
    public void setInhomogeneous(final boolean inhomogeneous) {
        this.inhomogeneous = inhomogeneous;
    }

    /**
     * Returns whether this node is atomic or not.
     *
     * <p>A node is atomic if it has only one child: the {@link EmptyNode}.</p>
     *
     * @return true if the node is atomic, false otherwise
     */
    public boolean isAtomic() {
        return children.size() == 1 && children.get(0) instanceof EmptyNode;
    }

    /**
     * Returns the pattern of the node.
     *
     * <p>The pattern will be an ampty list in case of an inhomogeneous node, or the concatenation of the prefix, core
     * and postfix parts of the stored rule.</p>
     *
     * <p>Note that the pattern is cached, so it's only evaluated on the first call.</p>
     *
     * @return the pattern of the node
     */
    public List<ICharacter> getPattern() {
        if (_pattern == null) {
            if (this.rule == null) {
                _pattern = List.of();
            }
            else {
                final int prefixSize = this.rule.getContext().getPrefix().size();
                final int coreSize = this.rule.getContext().getCore().size();
                final int postfixSize = this.rule.getContext().getPostfix().size();
                _pattern = new ArrayList<>(prefixSize + coreSize + postfixSize);
                _pattern.addAll(this.rule.getContext().getPrefix());
                _pattern.addAll(this.rule.getContext().getCore());
                _pattern.addAll(this.rule.getContext().getPostfix());
            }
        }

        return _pattern;
    }

    /**
     * Returns whether this node is already processed according to the given processing type and status.
     * @param processingType the process type
     * @param currentStatus the status
     * @return true if the stored status is greater than or equal to the given status
     */
    public boolean isProcessed(final ProcessingType processingType, final long currentStatus) {
        long previousStatus = 0L;

        if (this.processingStatuses.containsKey(processingType)) {
            previousStatus = this.processingStatuses.get(processingType);
        }

        return previousStatus >= currentStatus;
    }

    /**
     * Sets the given status to the given processing type.
     * @param processingType the processing type
     * @param currentStatus the new status value
     */
    public void setProcessingStatus(final ProcessingType processingType, final long currentStatus) {
        this.processingStatuses.put(processingType, currentStatus);
    }

    /**
     * Returns the level of the node.
     * @return the level of the node
     */
    public long getLevel() {
        return level;
    }

    /**
     * Sets the level of the node.
     * @param level the level of the node
     */
    public void setLevel(final long level) {
        this.level = level;
    }

    /**
     * Returns the frequency of the node.
     * @return the frequency of the node
     */
    public long getFrequency() {
        return frequency;
    }

    /**
     * Increments the frequency of the node by one.
     */
    public void incrementFrequency() {
        this.frequency++;
    }

    /**
     * Sets the frequency of the node.
     * @param frequency the frequency of the node
     */
    public void setFrequency(final long frequency) {
        this.frequency = frequency;
    }

    /**
     * Resets the node so that it can be reused.
     *
     * <p>The following things are done:</p>
     *
     * <ul>
     *     <li>The list of parents is cleared.</li>
     *     <li>The list of children is cleared.</li>
     *     <li>The inhomogeneous flag is set to false.</li>
     *     <li>The map of processing statuses is cleared.</li>
     *     <li>The level is reset to 0.</li>
     *     <li>The frequency is reset to 0.</li>
     * </ul>
     */
    public void reset() {
        this.parents.clear();
        this.children.clear();
        this.inhomogeneous = false;
        this.processingStatuses.clear();
        this.level = 0L;
        this.frequency = 0L;
    }

    /**
     * Returns the total count of equivalent attributes starting from the back of the pattern/characters until the first
     * non-matching character is encountered.
     *
     * @param characters the characters to check
     * @return the total number of equivalent attributes
     */
    public Integer getPartialMatchingFactor(final List<ICharacter> characters) {
        if (characters.isEmpty()) {
            return null;
        }

        final List<ICharacter> pattern = this.getPattern();

        if (pattern.isEmpty()) {
            return null;
        }

        if (pattern.get(0).isStart()) {
            pattern.remove(0);
        }

        if (pattern.isEmpty()) {
            return null;
        }

        if (pattern.get(pattern.size() - 1).isEnd()) {
            pattern.remove(pattern.size() - 1);
        }

        if (pattern.isEmpty()) {
            return null;
        }

        if (!SubsetCalculator.isSubsetOf(pattern.get(pattern.size() - 1), characters.get(characters.size() - 1))) {
            return null;
        }

        int result = 0;

        for (int indexFromBack = 0; indexFromBack < Math.min(characters.size(), pattern.size()); indexFromBack++) {
            final ICharacter patternCharacter = pattern.get(pattern.size() - indexFromBack - 1);
            final ICharacter character = characters.get(characters.size() - indexFromBack - 1);
            final Set<IAttribute> commonAttributes = new HashSet<>(patternCharacter.getAttributes());
            commonAttributes.retainAll(character.getAttributes());
            result += commonAttributes.size();

            if (!SubsetCalculator.isSubsetOf(patternCharacter, character)) {
                break;
            }
        }

        return result;
    }

    /**
     * Returns if this node should become inhomogeneous based on the given descendant. The result is
     * <code>true</code> if:
     *
     * <ul>
     *     <li>the descendant is inhomogeneous or</li>
     *     <li>the descendant is the {@link EmptyNode} or</li>
     *     <li>the transformations differ or</li>
     *     <li>this context does not contain the start or the end symbols and the contexts differ</li>
     * </ul>
     *
     * @param descendant the descendant node
     * @return true if this node should become inhomogeneous, false otherwise
     */
    public boolean shouldBecomeInhomogeneousBasedOnDescendant(final Node descendant) {
        if (descendant.isInhomogeneous()) {
            return true;
        }

        final Rule thisRule = this.getRule();
        final Rule descendantRule = descendant.getRule();

        if (descendant instanceof EmptyNode) {
            return false;
        }

        if (!Objects.equals(thisRule.getTransformations(), descendantRule.getTransformations())) {
            return true;
        }

        final Context thisContext = thisRule.getContext();

        final List<ICharacter> thisPrefix = thisContext.getPrefix();
        if (!thisPrefix.isEmpty() && thisPrefix.get(0).isStart()) {
            return false;
        }

        final List<ICharacter> thisPostfix = thisContext.getPostfix();
        if (!thisPostfix.isEmpty() && thisPostfix.get(thisPostfix.size() - 1).isEnd()) {
            return false;
        }

        return !thisContext.equals(descendantRule.getContext());
    }

    /**
     * Returns if this object and the given object equal.
     *
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        final Node node = (Node) o;
        return Objects.equals(rule, node.rule);
    }

    /**
     * Returns the hash code of this object.
     *
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        int result = rule != null ? rule.hashCode() : 0;
        result = 31 * result + getPattern().hashCode();
        return result;
    }

    /**
     * Returns the string representation of this object, i.e. the result of {@link Rule#toString()}.
     *
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        return "Node[rule=" + rule + ", inhomogeneous=" + inhomogeneous + "]";
    }

    /**
     * Returns a string in a hierarchical manner, starting from the given level and printing this node and its children,
     * indented.
     *
     * @param level the start level of the node
     * @return an indented resulting string
     */
    public String hierarchicalToString(final int level) {
        final StringBuilder sb = new StringBuilder();
        final String newLine = System.getProperty("line.separator");

        for (int i = 0; i < 2 * level; i++) {
            sb.append(' ');
        }
        sb.append(this.toString());
        sb.append(" - ");
        sb.append(this.getPattern());
        sb.append(newLine);

        for (final Node child : this.getChildren()) {
            sb.append(child.hierarchicalToString(level + 1));
        }

        return sb.toString();
    }

}
