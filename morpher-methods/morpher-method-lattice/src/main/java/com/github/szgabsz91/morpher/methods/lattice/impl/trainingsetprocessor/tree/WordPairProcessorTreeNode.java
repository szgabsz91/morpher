package com.github.szgabsz91.morpher.methods.lattice.impl.trainingsetprocessor.tree;

import com.github.szgabsz91.morpher.methods.lattice.impl.rules.transformations.ITransformation;

import java.util.ArrayList;
import java.util.List;
import java.util.Objects;
import java.util.function.Consumer;

/**
 * Tree node for the {@link WordPairProcessorTree} class.
 *
 * @author szgabsz91
 */
public class WordPairProcessorTreeNode implements Comparable<WordPairProcessorTreeNode> {

    private final int currentIndexInStartWord;
    private final int currentIndexInEndWord;
    private final ITransformation transformation;
    private final int scoreSoFar;
    private final int level;
    private final WordPairProcessorTreeNode parent;
    private final List<WordPairProcessorTreeNode> children;

    /**
     * Constructor that sets all properties.
     *
     * @param currentIndexInStartWord the current index in the start word
     * @param currentIndexInEndWord the current index in the end word
     * @param transformation the transformation of the node
     * @param scoreSoFar the score on the path so far
     * @param level the level of the node
     * @param parent the parent node
     */
    public WordPairProcessorTreeNode(
            final int currentIndexInStartWord,
            final int currentIndexInEndWord,
            final ITransformation transformation,
            final int scoreSoFar,
            final int level,
            final WordPairProcessorTreeNode parent) {
        this.currentIndexInStartWord = currentIndexInStartWord;
        this.currentIndexInEndWord = currentIndexInEndWord;
        this.transformation = transformation;
        this.scoreSoFar = scoreSoFar;
        this.level = level;
        this.parent = parent;
        this.children = new ArrayList<>();
    }

    /**
     * Default constructor for the root node.
     */
    public WordPairProcessorTreeNode() {
        this(0, 0, null, 0, 0, null);
    }

    /**
     * Returns the current index in the start word.
     * @return the current index in the start word
     */
    public int getCurrentIndexInStartWord() {
        return currentIndexInStartWord;
    }

    /**
     * Returns the current index in the end word.
     * @return the current index in the end word
     */
    public int getCurrentIndexInEndWord() {
        return currentIndexInEndWord;
    }

    /**
     * Returns the transformation.
     * @return the transformation
     */
    public ITransformation getTransformation() {
        return transformation;
    }

    /**
     * Returns the score on the path so far.
     * @return the score on the path so far
     */
    public int getScoreSoFar() {
        return scoreSoFar;
    }

    /**
     * Returns the level of the node.
     * @return the level of the node
     */
    public int getLevel() {
        return level;
    }

    /**
     * Returns the parent node.
     * @return the parent node
     */
    public WordPairProcessorTreeNode getParent() {
        return parent;
    }

    /**
     * Returns the children of the node.
     * @return the children of the node
     */
    public List<WordPairProcessorTreeNode> getChildren() {
        return children;
    }

    /**
     * Adds a new child to this node if it doesn't contain it already.
     * @param child the new child
     */
    public void addChild(final WordPairProcessorTreeNode child) {
        if (children.contains(child)) {
            return;
        }

        children.add(child);
    }

    /**
     * Returns if this node is the root node.
     * @return true if this node is the root node (i.e. it doesn't have a parent), false otherwise
     */
    public boolean isRoot() {
        return parent == null;
    }

    /**
     * Returns if this node is the leaf node.
     * @return true if this node is the leaf node (i.e. it doesn't have children, false otherwise
     */
    public boolean isLeaf() {
        return children.isEmpty();
    }

    /**
     * Traverses the node and its descendants in a pre-order manner, i.e. first processing the node and then process
     * the children.
     *
     * @param consumer the action that should be performed on each node
     */
    public void traversePreOrder(final Consumer<WordPairProcessorTreeNode> consumer) {
        consumer.accept(this);

        for (final WordPairProcessorTreeNode child : children) {
            child.traversePreOrder(consumer);
        }
    }

    /**
     * Traverses the node and its descendants in a post-order manner, i.e. first processing the children and then
     * process the node itself.
     *
     * @param consumer the action that should be performed on each node
     */
    public void traversePostOrder(final Consumer<WordPairProcessorTreeNode> consumer) {
        for (final WordPairProcessorTreeNode child : children) {
            child.traversePreOrder(consumer);
        }

        consumer.accept(this);
    }

    /**
     * Compares this object to the given {@link WordPairProcessorTreeNode} by comparing the <code>scoreSoFar</code>,
     * or if they equal, the <code>level</code> properties, the latter in opposite order.
     *
     * @param other the other {@link WordPairProcessorTreeNode}
     * @return the result of comparing, negative in case this object is less, positive otherwise, 0 in case of equality
     */
    @Override
    public int compareTo(final WordPairProcessorTreeNode other) {
        final int scoreSoFarResult = Integer.compare(scoreSoFar, other.scoreSoFar);
        if (scoreSoFarResult != 0) {
            return scoreSoFarResult;
        }

        return Integer.compare(other.level, level);
    }

    /**
     * Returns if this object equals the given other object.
     *
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        final WordPairProcessorTreeNode that = (WordPairProcessorTreeNode) o;
        return currentIndexInStartWord == that.currentIndexInStartWord &&
                currentIndexInEndWord == that.currentIndexInEndWord &&
                scoreSoFar == that.scoreSoFar &&
                level == that.level &&
                Objects.equals(transformation, that.transformation);
    }

    /**
     * Returns the hash code of this object.
     *
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        int result = currentIndexInStartWord;
        result = 31 * result + currentIndexInEndWord;
        result = 31 * result + (transformation != null ? transformation.hashCode() : 0);
        result = 31 * result + scoreSoFar;
        result = 31 * result + level;
        return result;
    }

    /**
     * Returns the string representation of this object, i.e. the result of the toString method on the transformation
     * if it's not null, or <code>[]</code> otherwise.
     *
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        if (transformation != null) {
            return transformation.toString();
        }

        return "[]";
    }

}
