package com.github.szgabsz91.morpher.transformationengines.lattice.converters;

import com.github.szgabsz91.morpher.core.io.IConverter;
import com.github.szgabsz91.morpher.transformationengines.api.characters.attributes.IAttribute;
import com.github.szgabsz91.morpher.transformationengines.api.characters.repositories.ICharacterRepository;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.Addition;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.AttributeDelta;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.ITransformation;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.Removal;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.Replacement;
import com.github.szgabsz91.morpher.transformationengines.lattice.protocolbuffers.TransformationMessage;
import com.google.protobuf.CodedInputStream;

import java.io.IOException;
import java.lang.reflect.InvocationTargetException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.Set;
import java.util.zip.GZIPInputStream;

import static java.util.stream.Collectors.toUnmodifiableSet;

/**
 * Protocol Buffer converter for the {@link ITransformation} interface and its subclasses.
 *
 * @author szgabsz91
 */
public class TransformationConverter implements IConverter<ITransformation, TransformationMessage> {

    private ICharacterRepository characterRepository;

    /**
     * Sets the underlying {@link ICharacterRepository} instance.
     * @param characterRepository the underlying {@link ICharacterRepository} instance
     */
    public void setCharacterRepository(final ICharacterRepository characterRepository) {
        this.characterRepository = characterRepository;
    }

    /**
     * Converts an {@link ITransformation} to a {@link TransformationMessage}.
     *
     * {@inheritDoc}
     */
    @Override
    public TransformationMessage convert(final ITransformation transformation) {
        final TransformationMessage.Builder builder = TransformationMessage.newBuilder();

        if (transformation instanceof Addition addition) {
            builder.setType(Addition.class.getName());
            addition.getAttributes()
                    .stream()
                    .map(attribute -> attribute.getClass().getName() + "." + attribute.toString())
                    .forEach(builder::addChange);
        }
        else if (transformation instanceof Removal removal) {
            builder.setType(Removal.class.getName());
            removal.getAttributes()
                    .stream()
                    .map(attribute -> attribute.getClass().getName() + "." + attribute.toString())
                    .forEach(builder::addChange);
        }
        else if (transformation instanceof Replacement replacement) {
            builder.setType(Replacement.class.getName());
            replacement.getAttributeDeltas()
                    .stream()
                    .map(attributeDelta -> {
                        return attributeDelta.getClazz().getName() + "." +
                                attributeDelta.getFrom() + "." +
                                attributeDelta.getTo();
                    })
                    .forEach(builder::addChange);
        }

        return builder.build();
    }

    /**
     * Converts a {@link TransformationMessage} to an {@link ITransformation}.
     *
     * {@inheritDoc}
     */
    @Override
    public ITransformation convertBack(final TransformationMessage transformationMessage) {
        final String type = transformationMessage.getType();

        if (Addition.class.getName().equals(type)) {
            final Set<IAttribute> attributes = getAttributes(transformationMessage);
            return new Addition(attributes, characterRepository);
        }

        if (Removal.class.getName().equals(type)) {
            final Set<IAttribute> attributes = getAttributes(transformationMessage);
            return new Removal(attributes, characterRepository);
        }

        if (Replacement.class.getName().equals(type)) {
            final Set<AttributeDelta<? super IAttribute>> attributeDeltas = transformationMessage.getChangeList()
                    .stream()
                    .map(change -> {
                        int lastDotIndex = change.lastIndexOf('.');
                        final String firstPart = change.substring(0, lastDotIndex);
                        final String toAttributeValue = change.substring(lastDotIndex + 1);
                        lastDotIndex = firstPart.lastIndexOf('.');
                        final String attributeType = firstPart.substring(0, lastDotIndex);
                        final String fromAttributeValue = firstPart.substring(lastDotIndex + 1);

                        try {
                            @SuppressWarnings("unchecked")
                            final Class<? extends IAttribute> attributeClass =
                                    (Class<? extends IAttribute>) Class.forName(attributeType);
                            final IAttribute fromAttribute = getAttributeValue(attributeType, fromAttributeValue);
                            final IAttribute toAttribute = getAttributeValue(attributeType, toAttributeValue);
                            @SuppressWarnings({ "rawtypes", "unchecked" })
                            final AttributeDelta<? super IAttribute> attributeDelta = new AttributeDelta(
                                    attributeClass,
                                    fromAttribute,
                                    toAttribute
                            );
                            return attributeDelta;
                        }
                        catch (final ClassNotFoundException e) {
                            throw new IllegalStateException("Attribute enum could not be instantiated", e);
                        }
                    })
                    .collect(toUnmodifiableSet());
            return new Replacement(attributeDeltas, characterRepository);
        }

        throw new IllegalStateException("Unknown transformation found: " + type);
    }

    /**
     * Parses the given file and loads the {@link TransformationMessage} from it.
     *
     * {@inheritDoc}
     */
    @Override
    public TransformationMessage parse(final Path file) throws IOException {
        try (GZIPInputStream gzipInputStream = new GZIPInputStream(Files.newInputStream(file))) {
            final CodedInputStream codedInputStream = CodedInputStream.newInstance(gzipInputStream);
            codedInputStream.setSizeLimit(Integer.MAX_VALUE);
            return TransformationMessage.parseFrom(codedInputStream);
        }
    }

    private static Set<IAttribute> getAttributes(final TransformationMessage transformationMessage) {
        return transformationMessage.getChangeList()
                .stream()
                .map(change -> {
                    final int lastDotIndex = change.lastIndexOf('.');
                    final String attributeType = change.substring(0, lastDotIndex);
                    final String attributeValue = change.substring(lastDotIndex + 1);
                    return getAttributeValue(attributeType, attributeValue);
                })
                .collect(toUnmodifiableSet());
    }

    private static IAttribute getAttributeValue(final String attributeType, final String attributeValue) {
        try {
            @SuppressWarnings({ "rawtypes", "unchecked" })
            final Class<? extends Enum> enumClass = (Class<? extends Enum>) Class.forName(attributeType);

            try {
                @SuppressWarnings("unchecked")
                final IAttribute result = (IAttribute) Enum.valueOf(
                        enumClass,
                        attributeValue
                );
                return result;
            }
            catch (final IllegalArgumentException e) {
                return (IAttribute) enumClass
                        .getDeclaredMethod("factory", String.class)
                        .invoke(null, attributeValue);
            }
        }
        catch (final ClassNotFoundException e) {
            throw new IllegalStateException("Attribute enum could not be instantiated", e);
        }
        catch (final NoSuchMethodException | IllegalAccessException | InvocationTargetException e) {
            throw new IllegalStateException(attributeType + "." + attributeValue + ".factory cannot be called", e);
        }
    }

}
