import lombok.RequiredArgsConstructor;

import java.util.Arrays;
import java.util.Collection;
import java.util.HashSet;
import java.util.List;
import java.util.Optional;
import java.util.PriorityQueue;
import java.util.Queue;
import java.util.Set;
import java.util.function.BiConsumer;
import java.util.function.BiFunction;
import java.util.function.Consumer;
import java.util.function.Function;
import java.util.function.Predicate;
import java.util.function.ToDoubleFunction;

import static java.util.Comparator.comparingDouble;
import static java.util.Comparator.naturalOrder;

/**
 * Walker class for the {@link Lattice} that can recursively traverse through a lattice and process
 * each node.
 *
 * <p>There are different methods for different traversal types, and also most of these methods take lambdas as
 * arguments that determine the direction, conditions and processing actions of the traversal.</p>
 *
 * @author szgabsz91
 */
@RequiredArgsConstructor
public class LatticeWalker {

    private final Lattice lattice;

    /**
     * Traverses the lattice from top to bottom and finds the first node that matches the given predicate.
     *
     * <p>If no nodes pass the predicate, the root node is returned.</p>
     *
     * @param nodePredicate predicate that must be true for the resulting node
     * @param stopPredicate predicate that will abort the search if a node passes it
     * @param partialMatchingFactorCalculator calculates an integer that denotes how much a node matches partially
     *                                        the input word
     * @return the resulting node if it passes the <code>nodePredicate</code>, or the root node
     */
    public Node findSingleNode(
            final Predicate<Node> nodePredicate,
            final Predicate<Node> stopPredicate,
            final Function<Node, Integer> partialMatchingFactorCalculator) {
        return findSingleGreedy(
                this.lattice.getUnitNode(),
                nodePredicate,
                stopPredicate,
                partialMatchingFactorCalculator,
                this.lattice.getCharacterRepository().getAttributeStatistics(),
                Node::getChildren
        );
    }

    /**
     * Traverses the lattice from the given {@link Node}, based on the lambdas given as parameters.
     *
     * @param startNode the start node of the traversal
     * @param nodePredicate predicate that all node on the path must pass
     * @param nextNodesSelector returns a list of candidates based on the current node for the next destinations
     * @param processingType the processing type
     * @param processingStatus the processing status
     * @param nodePostProcessor post processor for the nodes
     * @return a set of nodes that pass the predicate but none of its children do
     */
    public Set<Node> walk(
            final Node startNode,
            final Predicate<Node> nodePredicate,
            final Function<Node, Collection<Node>> nextNodesSelector,
            final ProcessingType processingType,
            final long processingStatus,
            final Consumer<Node> nodePostProcessor) {
        final Set<Node> result = new HashSet<>();
        processNodes(
                startNode,
                nextNodesSelector,
                nodePredicate,
                node -> node.isProcessed(processingType, processingStatus),
                node -> node.setProcessingStatus(processingType, processingStatus),
                result::add,
                nodePostProcessor
        );
        return result;
    }

    /**
     * Traverses the lattice from the given {@link Node}, based on the lambdas given as parameters.
     *
     * @param startNode the start node of the traversal
     * @param nodePredicate predicate that all node on the path must pass
     * @param nextNodesSelector returns a list of candidates based on the current node for the next destinations
     * @param processingType the processing type
     * @param processingStatus the processing status
     * @return a set of nodes that pass the predicate but none of its children do
     */
    public Set<Node> walk(
            final Node startNode,
            final Predicate<Node> nodePredicate,
            final Function<Node, Collection<Node>> nextNodesSelector,
            final ProcessingType processingType,
            final long processingStatus) {
        return walk(
                startNode,
                nodePredicate,
                nextNodesSelector,
                processingType,
                processingStatus,
                null
        );
    }

    /**
     * Traverses the lattice and processes each node using the given consumer.
     *
     * <p>Note that this method only processes each node once.</p>
     *
     * <p>The start node will be the root and the processing will take place from top to bottom.</p>
     *
     * @param consumer the action that needs to be done for each node of the lattice
     */
    public void processNodesOnce(final Consumer<Node> consumer) {
        this.processNodesOnce(
                this.lattice.getUnitNode(),
                Node::getChildren,
                consumer
        );
    }

    /**
     * Traverses the lattice and processes each node using the given consumer, from the given node to the
     * given direction.
     *
     * <p>Note that this method only processes each node once.</p>
     *
     * @param startNode the node to start from
     * @param nextNodesSelector selects the next nodes from the current node
     * @param consumer the action that needs to be done for each node of the lattice
     */
    public void processNodesOnce(
            final Node startNode,
            final Function<Node, Collection<Node>> nextNodesSelector,
            final Consumer<Node> consumer) {
        final long processingStatus = this.lattice.getNextProcessingStatus(ProcessingType.GENERAL_PROCESSING);
        processNodes(
                startNode,
                nextNodesSelector,
                node -> !node.isProcessed(ProcessingType.GENERAL_PROCESSING, processingStatus),
                node -> node.isProcessed(ProcessingType.GENERAL_PROCESSING, processingStatus),
                node -> {
                    node.setProcessingStatus(ProcessingType.GENERAL_PROCESSING, processingStatus);
                    consumer.accept(node);
                }
        );
    }

    /**
     * Fills the node levels from the root to the leaf. Every node will contain the maximal length of path from the root
     * to the node. The root's level will be zero. This method needs to be invoked before
     * {@link LatticeWalker#fillDominantRules()} because it depends on the node levels.
     */
    public void fillNodeLevels() {
        processNodes(
                this.lattice.getUnitNode(),
                0L,
                Node::getChildren,
                (node, currentLevel) -> node.getLevel() < currentLevel,
                currentLevel -> currentLevel + 1L,
                Node::setLevel
        );
    }

    /**
     * Fills the dominant rules of inconsistent nodes in the lattice.
     *
     * <p>First, the inconsistent nodes are collected and sorted based on their level. Then, the nodes are processed
     * in the descending order of their level and their child with the highest frequency is selected to fetch the
     * dominant rule. The rule is set in the node and the process continues with the next inconsistent node in the
     * queue.</p>
     */
    public void fillDominantRules() {
        final Queue<Node> inconsistentNodes = new PriorityQueue<>(
                (n1, n2) -> Long.compare(n2.getLevel(), n1.getLevel())
        );
        inconsistentNodes.addAll(List.of(lattice.getInconsistentNodes()));

        while (!inconsistentNodes.isEmpty()) {
            final Node inconsistentNode = inconsistentNodes.poll();

            final Node[] candidateNodes = inconsistentNode.getChildren()
                    .toArray(new Node[0]);

            final Rule chosenRule = chooseDominantRule(candidateNodes);

            if (chosenRule == null || chosenRule.getTransformations() == null) {
                throw new IllegalStateException("The chosen rule has no transformations: " + chosenRule);
            }

            if (inconsistentNode.getRule() == null) {
                throw new IllegalStateException(
                        "The node is not the root but still has no rule: " + inconsistentNode
                );
            }
            else if (inconsistentNode.getRule().getTransformations() == null) {
                inconsistentNode.getRule().setTransformations(chosenRule.getTransformations());
            }
        }
    }

    private static Rule chooseDominantRule(final Node[] candidateNodes) {
        Node chosenNode = candidateNodes[0];

        for (int i = 1; i < candidateNodes.length; i++) {
            final Node candidateNode = candidateNodes[i];

            if (candidateNode.getRule().getTransformations() == null) {
                throw new IllegalStateException(
                        "Theoretically a candidate must have a transformation as the processing takes place bottom-up"
                );
            }

            if (candidateNode.getFrequency() > chosenNode.getFrequency()) {
                chosenNode = candidateNode;
            }
        }

        return chosenNode.getRule();
    }

    /**
     * Returns the set of maximal consistent nodes, i.e. the nodes that are consistent but none of its ancestors are.
     * @return the set of maximal consistent nodes
     */
    public Set<Node> getMaximalConsistentNodes() {
        return collectUntil(
                this.lattice.getUnitNode(),
                Node::isInconsistent,
                node -> node.getParents().stream().noneMatch(Node::isConsistent),
                Node::getChildren
        );
    }

    private static void processNodes(
            final Node node,
            final Function<Node, Collection<Node>> nextNodesSelector,
            final Predicate<Node> nodePredicate,
            final Predicate<Node> skipNodePredicate,
            final Consumer<Node> nodePreProcessor,
            final Consumer<Node> noNextNodesProcessor,
            final Consumer<Node> nodePostProcessor) {
        if (skipNodePredicate.test(node)) {
            return;
        }

        nodePreProcessor.accept(node);

        final Node[] nextNodes = nextNodesSelector.apply(node)
                .stream()
                .filter(nodePredicate)
                .toArray(Node[]::new);

        if (noNextNodesProcessor != null && nextNodes.length == 0) {
            noNextNodesProcessor.accept(node);
            return;
        }

        Arrays.stream(nextNodes).forEach(nextNode -> {
            processNodes(
                    nextNode,
                    nextNodesSelector,
                    nodePredicate,
                    skipNodePredicate,
                    nodePreProcessor,
                    noNextNodesProcessor,
                    nodePostProcessor
            );
        });

        if (nodePostProcessor != null) {
            nodePostProcessor.accept(node);
        }
    }

    private static void processNodes(
            final Node node,
            final Function<Node, Collection<Node>> nextNodesSelector,
            final Predicate<Node> nodePredicate,
            final Predicate<Node> skipNodePredicate,
            final Consumer<Node> nodePreProcessor) {
        processNodes(
                node,
                nextNodesSelector,
                nodePredicate,
                skipNodePredicate,
                nodePreProcessor,
                null,
                null
        );
    }

    private static <T> void processNodes(
            final Node node,
            final T data,
            final Function<Node, Collection<Node>> nextNodesSelector,
            final BiFunction<Node, T, Boolean> nodePredicate,
            final Function<T, T> dataTransformer,
            final BiConsumer<Node, T> nodePreProcessor) {
        nodePreProcessor.accept(node, data);

        final T nextData = dataTransformer.apply(data);

        nextNodesSelector.apply(node)
                .stream()
                .filter(nextNode -> nodePredicate.apply(nextNode, nextData))
                .forEach(nextNode -> {
                    processNodes(
                            nextNode,
                            nextData,
                            nextNodesSelector,
                            nodePredicate,
                            dataTransformer,
                            nodePreProcessor
                    );
                });
    }

    private static Set<Node> collectUntil(
            final Node node,
            final Predicate<Node> negativeNodePredicate,
            final Predicate<Node> positiveNodePredicate,
            final Function<Node, Collection<Node>> nextNodesSelector) {
        final Set<Node> result = new HashSet<>();
        findUntil(
                node,
                negativeNodePredicate,
                nextNodesSelector,
                n -> {
                    if (positiveNodePredicate.test(n)) {
                        result.add(n);
                    }
                }
        );
        return result;
    }

    private static void findUntil(
            final Node node,
            final Predicate<Node> nodePredicate,
            final Function<Node, Collection<Node>> nextNodesSelector,
            final Consumer<Node> foundNodeConsumer) {
        if (!nodePredicate.test(node)) {
            foundNodeConsumer.accept(node);
            return;
        }

        nextNodesSelector.apply(node)
                .forEach(nextNode -> {
                    findUntil(
                            nextNode,
                            nodePredicate,
                            nextNodesSelector,
                            foundNodeConsumer
                    );
                });
    }

    private Node findSingleGreedy(
            final Node node,
            final Predicate<Node> nodePredicate,
            final Predicate<Node> stopPredicate,
            final Function<Node, Integer> partialMatchingFactorCalculator,
            final IAttributeStatistics attributeStatistics,
            final Function<Node, Collection<Node>> nextNodesSelector) {
        final Optional<Node> nextConsistentNodeOptional = nextNodesSelector.apply(node)
                .stream()
                .filter(nodePredicate.and(stopPredicate))
                .findFirst();

        if (nextConsistentNodeOptional.isPresent()) {
            return nextConsistentNodeOptional.get();
        }

        final ToDoubleFunction<? super Node> keyExtractor = n -> n.getPattern()
                .stream()
                .mapToDouble(attributeStatistics::getRelativeFrequency)
                .reduce(1.0, (x, y) -> x * y);

        final Optional<Node> nextNodeOptional = nextNodesSelector.apply(node)
                .stream()
                .filter(nodePredicate)
                .min(comparingDouble(keyExtractor));

        if (nextNodeOptional.isPresent()) {
            return findSingleGreedy(
                    nextNodeOptional.get(),
                    nodePredicate,
                    stopPredicate,
                    partialMatchingFactorCalculator,
                    attributeStatistics,
                    nextNodesSelector
            );
        }

        final Optional<PartiallyMatchingNode> partiallyMatchingNode = node.getChildren()
                .stream()
                .map(n -> new PartiallyMatchingNode(n, partialMatchingFactorCalculator.apply(n)))
                .filter(matchingNode -> matchingNode.getMatchingFactor() != null)
                .max(naturalOrder());

        if (partiallyMatchingNode.isPresent()) {
            return partiallyMatchingNode.get().getNode();
        }

        return node;
    }

    /**
     * Returns those maximal consistent nodes of the lattice that are reachable from the given node, i.e.
     * are the descendants of it.
     *
     * @param node the topmost node
     * @return the array of maximal consistent descendants
     */
    public Node[] getMaximalConsistentDescendants(final Node node) {
        return getMaximalConsistentNodes()
                .stream()
                .filter(currentNode -> SubsetCalculator.isSubsetOf(node, currentNode))
                .toArray(Node[]::new);
    }

    /**
     * Wrapper class for a {@link Node} and its matching factor.
     * @author szgabsz91
     */
    @Data
    @RequiredArgsConstructor(access = AccessLevel.PRIVATE)
    @Getter(AccessLevel.PRIVATE)
    @EqualsAndHashCode
    private static final class PartiallyMatchingNode implements Comparable<PartiallyMatchingNode> {

        private final Node node;
        private final Integer matchingFactor;

        @Override
        public int compareTo(final PartiallyMatchingNode other) {
            return Integer.compare(matchingFactor, other.matchingFactor);
        }

    }

}
