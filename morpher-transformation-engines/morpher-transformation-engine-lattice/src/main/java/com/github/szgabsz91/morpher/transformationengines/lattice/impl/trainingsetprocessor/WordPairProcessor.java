package com.github.szgabsz91.morpher.transformationengines.lattice.impl.trainingsetprocessor;

import com.github.szgabsz91.morpher.core.model.WordPair;
import com.github.szgabsz91.morpher.transformationengines.api.characters.ICharacter;
import com.github.szgabsz91.morpher.transformationengines.api.characters.repositories.ICharacterRepository;
import com.github.szgabsz91.morpher.transformationengines.api.wordconverters.IWordConverter;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.Context;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.Position;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.Rule;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.ITransformation;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.trainingsetprocessor.costcalculator.ICostCalculator;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.trainingsetprocessor.model.WordPairProcessorResponse;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.trainingsetprocessor.tree.WordPairProcessorTree;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.trainingsetprocessor.tree.WordPairProcessorTreeNode;

import java.util.ArrayList;
import java.util.LinkedList;
import java.util.List;
import java.util.stream.IntStream;

import static java.util.stream.Collectors.toList;

/**
 * The default {@link IWordPairProcessor} implementation class.
 *
 * @author szgabsz91
 */
public final class WordPairProcessor implements IWordPairProcessor {

    private final ICharacterRepository characterRepository;
    private final IWordConverter wordConverter;
    private final ICostCalculator costCalculator;
    private final int pathLevelsToProcess;
    private final int maximalContextSize;

    private WordPairProcessor(
            final ICharacterRepository characterRepository,
            final IWordConverter wordConverter,
            final ICostCalculator costCalculator,
            final int pathLevelsToProcess,
            final int maximalContextSize) {
        this.characterRepository = characterRepository;
        this.wordConverter = wordConverter;
        this.costCalculator = costCalculator;
        this.pathLevelsToProcess = pathLevelsToProcess;
        this.maximalContextSize = maximalContextSize;
    }

    /**
     * Returns the underlying {@link ICharacterRepository}.
     * @return the underlying {@link ICharacterRepository}
     */
    public ICharacterRepository getCharacterRepository() {
        return characterRepository;
    }

    /**
     * Returns the underlying {@link IWordConverter}.
     * @return the underlying {@link IWordConverter}
     */
    public IWordConverter getWordConverter() {
        return wordConverter;
    }

    /**
     * Returns the underlying {@link ICostCalculator}.
     * @return the underlying {@link ICostCalculator}
     */
    public ICostCalculator getCostCalculator() {
        return costCalculator;
    }

    /**
     * Returns the path levels to process.
     * @return the path levels to process
     */
    int getPathLevelsToProcess() {
        return pathLevelsToProcess;
    }

    /**
     * Returns the maximal context size.
     * @return the maximal context size
     */
    public int getMaximalContextSize() {
        return maximalContextSize;
    }

    /**
     * Returns the resulting {@link WordPairProcessorResponse} object after processing the given {@link WordPair}.
     *
     * {@inheritDoc}
     */
    @Override
    public WordPairProcessorResponse induceRules(final WordPair wordPair) {
        final List<ICharacter> leftWordCharacters = this.wordConverter.convert(
                wordPair.getLeftWord(),
                characterRepository
        );
        final List<ICharacter> rightWordCharacters = this.wordConverter.convert(
                wordPair.getRightWord(),
                characterRepository
        );
        final WordPairProcessorTree tree = this.createWordPairProcessorTree(leftWordCharacters, rightWordCharacters);
        final List<WordPairProcessorTreeNode> leaves = tree.build(pathLevelsToProcess);
        final List<Rule> rules = new ArrayList<>();
        for (final WordPairProcessorTreeNode leaf : leaves) {
            final List<WordPairProcessorTreeNode> path = this.getPath(leaf);
            int removedFromBeginning = this.removeInvariantPartsFromBeginning(path);
            int removedFromEnd = this.removeInvariantPartsFromEnd(path);
            final List<ICharacter> word = new ArrayList<>(leftWordCharacters);
            word.add(0, characterRepository.getStartCharacter());
            word.add(characterRepository.getEndCharacter());
            removedFromBeginning++;
            removedFromEnd++;
            final List<ICharacter> fullPrefix = word
                    .stream()
                    .limit(removedFromBeginning)
                    .collect(toList());
            List<ICharacter> prefix = fullPrefix;
            if (maximalContextSize != Integer.MAX_VALUE) {
                prefix = fullPrefix
                        .stream()
                        .skip(Math.max(0, removedFromBeginning - maximalContextSize))
                        .limit(Math.min(maximalContextSize, removedFromBeginning))
                        .collect(toList());
            }
            final List<ICharacter> core = word
                    .stream()
                    .skip(removedFromBeginning)
                    .limit(word.size() - removedFromEnd - removedFromBeginning)
                    .collect(toList());
            final List<ICharacter> fullPostfix = word
                    .stream()
                    .skip(word.size() - removedFromEnd)
                    .collect(toList());
            List<ICharacter> postfix = fullPostfix;
            if (maximalContextSize != Integer.MAX_VALUE) {
                postfix = fullPostfix
                        .stream()
                        .limit(maximalContextSize)
                        .collect(toList());
            }
            final List<ICharacter> contextCharacters = new ArrayList<>(
                    prefix.size() + core.size() + postfix.size()
            );
            contextCharacters.addAll(prefix);
            contextCharacters.addAll(core);
            contextCharacters.addAll(postfix);
            final Position frontPosition = this.getFrontPosition(word, contextCharacters, removedFromBeginning);
            final Position backPosition = this.getBackPosition(word, contextCharacters, core, removedFromBeginning);
            final Context context = new Context(prefix, core, postfix, frontPosition, backPosition);
            final List<ITransformation> transformations = path
                    .stream()
                    .map(WordPairProcessorTreeNode::getTransformation)
                    .collect(toList());
            final Rule rule = new Rule(context, transformations, characterRepository, wordConverter);
            rules.add(rule);
        }

        return new WordPairProcessorResponse(tree, leaves, rules);
    }

    private WordPairProcessorTree createWordPairProcessorTree(
            final List<ICharacter> leftWordCharacters,
            final List<ICharacter> rightWordCharacters) {
        return new WordPairProcessorTree(
                new WordPairProcessorTreeNode(),
                leftWordCharacters,
                rightWordCharacters,
                characterRepository,
                costCalculator
        );
    }

    private List<WordPairProcessorTreeNode> getPath(final WordPairProcessorTreeNode leaf) {
        final List<WordPairProcessorTreeNode> path = new LinkedList<>();
        WordPairProcessorTreeNode node = leaf;

        while (!node.isRoot()) {
            path.add(0, node);
            node = node.getParent();
        }

        return path;
    }

    private int removeInvariantPartsFromBeginning(final List<WordPairProcessorTreeNode> path) {
        int counter = 0;

        while (path.get(0).getTransformation().isInconsistent()) {
            path.remove(0);
            counter++;
        }

        return counter;
    }

    private int removeInvariantPartsFromEnd(final List<WordPairProcessorTreeNode> path) {
        int counter = 0;

        while (path.get(path.size() - 1).getTransformation().isInconsistent()) {
            path.remove(path.size() - 1);
            counter++;
        }

        return counter;
    }

    private Position getFrontPosition(
            final List<ICharacter> word,
            final List<ICharacter> context,
            final int removedFromBeginning) {
        if (context.isEmpty()) {
            throw new IllegalArgumentException("The context cannot be empty");
        }

        int matchCounter = 0;

        if (word.size() > context.size()) {
            for (int i = 0; i < removedFromBeginning; i++) {
                final List<ICharacter> foundCharacters = word.subList(i, Math.min(i + context.size(), word.size()));

                if (foundCharacters.equals(context)) {
                    matchCounter++;
                }
            }
        }
        // This condition must be true in this case: word.size() == context.size() && word.equals(context)
        else {
            matchCounter++;
        }

        return Position.of(matchCounter - 1);
    }

    private Position getBackPosition(
            final List<ICharacter> word,
            final List<ICharacter> context,
            final List<ICharacter> core,
            final int removedFromBeginning) {
        if (context.isEmpty()) {
            throw new IllegalArgumentException("The context cannot be empty");
        }

        final int removedFromEnd = word.size() - removedFromBeginning - core.size();

        if (removedFromEnd == 0) {
            throw new IllegalStateException("Did not remove anything from end");
        }

        final List<ICharacter> wordReverse = IntStream.range(0, word.size())
                .map(index -> word.size() - index - 1)
                .mapToObj(word::get)
                .collect(toList());
        final List<ICharacter> contextReverse = IntStream.range(0, context.size())
                .map(index -> context.size() - index - 1)
                .mapToObj(context::get)
                .collect(toList());

        return this.getFrontPosition(wordReverse, contextReverse, removedFromEnd);
    }

    /**
     * Builder class for the {@link WordPairProcessor} class.
     *
     * @author szgabsz91
     */
    public static class Builder {

        private ICharacterRepository characterRepository;
        private IWordConverter wordConverter;
        private ICostCalculator costCalculator;
        private int pathLevelsToProcess;
        private int maximalContextSize;

        /**
         * Constructor that sets the optional properties to their default values.
         */
        public Builder() {
            this.pathLevelsToProcess = 1;
            this.maximalContextSize = Integer.MAX_VALUE;
        }

        /**
         * Sets the {@link ICharacterRepository} instance.
         * @param characterRepository the {@link ICharacterRepository} instance
         * @return this object
         */
        public Builder characterRepository(final ICharacterRepository characterRepository) {
            this.characterRepository = characterRepository;
            return this;
        }

        /**
         * Sets the {@link IWordConverter} instance.
         * @param wordConverter the {@link IWordConverter} instance
         * @return this object
         */
        public Builder wordConverter(final IWordConverter wordConverter) {
            this.wordConverter = wordConverter;
            return this;
        }

        /**
         * Sets the {@link ICostCalculator} instance.
         * @param costCalculator the {@link ICostCalculator} instance
         * @return this object
         */
        public Builder costCalculator(final ICostCalculator costCalculator) {
            this.costCalculator = costCalculator;
            return this;
        }

        /**
         * Sets the number of path levels to process.
         * @param pathLevelsToProcess the number of path levels to process
         * @return this object
         */
        public Builder pathLevelsToProcess(final int pathLevelsToProcess) {
            this.pathLevelsToProcess = pathLevelsToProcess;
            return this;
        }

        /**
         * Sets the maximal context size.
         * @param maximalContextSize the maximal context size
         * @return this object
         */
        public Builder maximalContextSize(final int maximalContextSize) {
            this.maximalContextSize = maximalContextSize;
            return this;
        }

        /**
         * Builds the {@link IWordPairProcessor} instance.
         *
         * <p>Note that the character repository, word converter and cost calculator must be set or an
         * {@link IllegalStateException} is thrown.</p>
         *
         * @return the newly created {@link WordPairProcessor} object
         */
        public IWordPairProcessor build() {
            if (characterRepository == null) {
                throw new IllegalStateException("There is no character repository but it is mandatory");
            }

            if (wordConverter == null) {
                throw new IllegalStateException("There is no word converter but it is mandatory");
            }

            if (costCalculator == null) {
                throw new IllegalStateException("There is no cost calculator but it is mandatory");
            }

            return new WordPairProcessor(
                    characterRepository,
                    wordConverter,
                    costCalculator,
                    pathLevelsToProcess,
                    maximalContextSize
            );
        }

    }

}
