package com.github.szgabsz91.morpher.transformationengines.lattice.impl;

import com.github.szgabsz91.morpher.core.model.Word;
import com.github.szgabsz91.morpher.transformationengines.api.characters.ICharacter;
import com.github.szgabsz91.morpher.transformationengines.api.characters.repositories.ICharacterRepository;
import com.github.szgabsz91.morpher.transformationengines.api.wordconverters.IWordConverter;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.builders.ILatticeBuildListener;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.nodes.Node;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.nodes.UnitNode;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.nodes.ZeroNode;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.setoperations.SubsetCalculator;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collection;
import java.util.HashMap;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;
import java.util.Objects;
import java.util.Set;
import java.util.stream.Stream;

import static java.util.stream.Collectors.toUnmodifiableSet;

/**
 * Lattice implementation class.
 *
 * @author szgabsz91
 */
public class Lattice {

    private static final Logger LOGGER = LoggerFactory.getLogger(Lattice.class);

    private final Map<List<ICharacter>, Node> nodeMap;
    private final Node unitNode;
    private final Node zeroNode;
    private final ICharacterRepository characterRepository;
    private final IWordConverter wordConverter;
    private final Map<ProcessingType, Long> latestProcessingStatuses;
    private LatticeWalker latticeWalker;
    private ILatticeBuildListener buildListener;

    /**
     * Constructor that sets the unit node, the zero node and a flag that indicates if the {@link LatticeWalker} should
     * be used for parent/child search or not.
     *
     * <p>As a side effect, sets the starting relationship between the unit node and the zero node.</p>
     *
     * @param unitNode the unit node
     * @param zeroNode the zero node
     * @param characterRepository the {@link ICharacterRepository} instance
     * @param wordConverter the {@link IWordConverter} instance
     */
    public Lattice(
            final Node unitNode,
            final Node zeroNode,
            final ICharacterRepository characterRepository,
            final IWordConverter wordConverter) {
        this.nodeMap = new LinkedHashMap<>();
        unitNode.addChild(zeroNode);
        this.unitNode = unitNode;
        this.zeroNode = zeroNode;
        this.characterRepository = characterRepository;
        this.wordConverter = wordConverter;
        this.latestProcessingStatuses = new HashMap<>();
        this.latticeWalker = new LatticeWalker(this);
    }

    /**
     * Returns the next processing status for the given processing type.
     * @param processingType the processing type
     * @return the next processing status
     */
    public long getNextProcessingStatus(final ProcessingType processingType) {
        Long latestProcessingStatus = this.latestProcessingStatuses.get(processingType);
        if (latestProcessingStatus == null) {
            latestProcessingStatus = 0L;
        }
        final long nextProcessingStatus = latestProcessingStatus + 1;
        this.latestProcessingStatuses.put(processingType, nextProcessingStatus);
        return nextProcessingStatus;
    }

    /**
     * Returns the unit node.
     * @return the unit node
     */
    public Node getUnitNode() {
        return unitNode;
    }

    /**
     * Returns the zero node.
     * @return the zero node
     */
    public Node getZeroNode() {
        return zeroNode;
    }

    /**
     * Returns the list of nodes.
     *
     * <p>Note that this list doesn't contain the unit node and the zero node nodes.</p>
     *
     * @return the list of nodes except for the unit node and the zero node
     */
    public Collection<Node> getNodes() {
        return this.nodeMap.values();
    }

    /**
     * Adds the given nodes to the node map if no nodes with their patterns exist yet. Otherwise, a new
     * {@link IllegalArgumentException} is thrown.
     *
     * <p>Note that the relationships among nodes are not updated by this method. If it is required, use
     * {@link Lattice#insertNode(Node)} instead.</p>
     *
     * @param nodes the new nodes to add
     */
    public void addNodes(final Node[] nodes) {
        Arrays.stream(nodes).forEach(this::addNode);
    }

    /**
     * Adds the given node to the node map if no nodes with its pattern exist yet. Otherwise, a new
     * {@link IllegalArgumentException} is thrown.
     *
     * <p>Note that the relationships among nodes are not updated by this method. If it is required, use
     * {@link Lattice#insertNode(Node)} instead.</p>
     *
     * @param node the new node
     */
    public void addNode(final Node node) {
        final List<ICharacter> pattern = node.getPattern();

        if (this.nodeMap.containsKey(pattern)) {
            throw new IllegalArgumentException(
                    "A node with pattern " + pattern + " is already present, cannot add new node " + node
            );
        }

        this.nodeMap.put(pattern, node);
    }

    /**
     * Returns if the given node is present in this lattice or not.
     *
     * <p>If the node is a {@link UnitNode} or a {@link ZeroNode}, the result is true by default.</p>
     *
     * @param node the node to check
     * @return true if the node is present in the lattice
     */
    public boolean hasNode(final Node node) {
        if (node instanceof UnitNode || node instanceof ZeroNode) {
            return true;
        }

        return this.nodeMap.containsKey(node.getPattern());
    }

    /**
     * Returns a {@link Node} stream containing all the nodes in the lattice except for the given node, the unit node
     * and the zero node.
     *
     * <p>Note that equality is checked with reference equality and not by calling {@link Object#equals(Object)}.</p>
     *
     * @param node the node to drop from the stream
     * @return the stream of resulting {@link Node} objects
     */
    public Stream<Node> getNodesExceptFor(final Node node) {
        return this.getNodes()
                .stream()
                .filter(n -> n != node);
    }

    /**
     * Returns the inconsistent nodes between the unit node and the zero node.
     * @return the inconsistent nodes
     */
    public Node[] getInconsistentNodes() {
        return this.getNodes()
                .stream()
                .filter(Node::isInconsistent)
                .toArray(Node[]::new);
    }

    /**
     * Returns the atomic nodes in the lattice, i.e. the parents of the zero node.
     * @return the atomic nodes
     */
    public Node[] getAtomicNodes() {
        return this.zeroNode.getParents()
                .stream()
                .filter(node -> !(node instanceof UnitNode))
                .toArray(Node[]::new);
    }

    /**
     * Returns the size of the lattice, i.e. the number of normal nodes plus two (the unit node and the zero node).
     * @return the size of the lattice
     */
    public int size() {
        return this.nodeMap.size() + 2;
    }

    /**
     * Returns if the lattice is empty other than the {@link UnitNode} and the {@link ZeroNode}.
     * @return true if the lattice doesn't contain any nodes other than the unit node and the zero node, false otherwise
     */
    public boolean isEmpty() {
        return this.nodeMap.isEmpty();
    }

    /**
     * Returns the underlying {@link ICharacterRepository} instance.
     * @return the underlying {@link ICharacterRepository} instance
     */
    public ICharacterRepository getCharacterRepository() {
        return characterRepository;
    }

    /**
     * Returns the underlying {@link IWordConverter} instance.
     * @return the underlying {@link IWordConverter} instance
     */
    public IWordConverter getWordConverter() {
        return wordConverter;
    }

    /**
     * Returns the {@link ILatticeBuildListener} instance.
     * @return the {@link ILatticeBuildListener} instance
     */
    ILatticeBuildListener getBuildListener() {
        return buildListener;
    }

    /**
     * Returns if this {@link Lattice} matches the given {@link Word} and thus can be used for the next step of
     * morpheme analysis.
     *
     * <p>The current implementation returns <code>true</code> if any of the nodes under the unit node matches the word,
     * i.e. the node's rule has a matching pattern.</p>
     *
     * @param word the word to check
     * @return true if this {@link Lattice} matches the {@link Word}, false otherwise
     */
    public boolean matches(final Word word) {
        final Word convertedWord = this.wordConverter.convert(word);
        return this.getUnitNode().getChildren()
                .stream()
                .anyMatch(node -> node.getRule() != null && node.getRule().matches(convertedWord));
    }

    /**
     * Inserts the given node into the lattice.
     *
     * <p>First, the parents and children are searched for in the lattice and some checks are performed against them.
     * In case of any errors, a {@link RuntimeException} is thrown.</p>
     *
     * <p>If there are no problems, the new node is inserted. However, if the node makes an existing node inconsistent,
     * that existing node and all of its ancestors up to the unit node are made inconsistent.</p>
     *
     * <p>After the node is inserted, the ancestors' frequencies are incremented by one.</p>
     *
     * @param node the node to be inserted
     * @return true if the node is inserted, false if it existed before
     */
    public boolean insertNode(final Node node) {
        final Node existingNode = this.nodeMap.get(node.getPattern());

        if (existingNode != null) {
            if (Objects.equals(node.getRule().getTransformations(), existingNode.getRule().getTransformations())) {
                return false;
            }

            if (this.buildListener != null) {
                this.buildListener.onNodeBecomingInconsistent(this, existingNode);
                return true;
            }
        }

        // Collect children
        final Set<Node> childrenOfNewNode = latticeWalker.walk(
                this.getZeroNode(),
                n -> SubsetCalculator.isSubsetOf(node, n),
                Node::getParents,
                ProcessingType.CHILD_LIST_SEARCH,
                this.getNextProcessingStatus(ProcessingType.CHILD_LIST_SEARCH)
        );
        if (this.buildListener != null &&
                this.buildListener.skipNodeInserting(node, childrenOfNewNode)) {
            return true;
        }
        if (childrenOfNewNode.isEmpty()) {
            throw new IllegalStateException(
                    "The children of new node " + node + " is empty, but at least the zero node should be in this set"
            );
        }

        // Collect parents
        final Set<Node> parentsOfNewNode = latticeWalker.walk(
                this.getUnitNode(),
                n -> SubsetCalculator.isSubsetOf(n, node),
                Node::getChildren,
                ProcessingType.PARENT_LIST_SEARCH,
                this.getNextProcessingStatus(ProcessingType.PARENT_LIST_SEARCH)
        );
        if (parentsOfNewNode.isEmpty()) {
            throw new IllegalStateException(
                    "The parents of new node " + node + " is empty, but at least the unit node should be in this set"
            );
        }

        // Check if there are common nodes in the set of parents and children
        final List<Node> commonNodes = new ArrayList<>(parentsOfNewNode);
        commonNodes.retainAll(childrenOfNewNode);
        if (!commonNodes.isEmpty()) {
            if (commonNodes.size() == 1) {
                if (this.buildListener != null) {
                    this.buildListener.onNodeBecomingInconsistent(this, commonNodes.get(0));
                    this.buildListener.onNodeInserted(this, node);
                }
                return true;
            }
            else {
                throw new IllegalStateException(
                        String.format(
                                "More than 1 common nodes, needs investigation!%n\tNode: %s%n\tParents: %s%n" +
                                        "\tChildren: %s%n\tCommon: %s",
                                node, parentsOfNewNode, childrenOfNewNode, commonNodes
                        )
                );
            }
        }

        final Set<Node> ambiguousPaths = parentsOfNewNode
                .stream()
                .flatMap(p -> p.getChildren().stream())
                .filter(n -> n.getRule() != null)
                .filter(n -> {
                    return n.getRule().getContext().equals(node.getRule().getContext()) &&
                            !n.getRule().getTransformations().equals(node.getRule().getTransformations());
                })
                .collect(toUnmodifiableSet());
        if (!ambiguousPaths.isEmpty()) {
            if (this.buildListener != null) {
                ambiguousPaths.forEach(n -> this.buildListener.onNodeBecomingInconsistent(this, n));
                this.buildListener.onNodeInserted(this, node);
            }
            return true;
        }

        for (final Node parent : parentsOfNewNode) {
            parent.removeChildren(childrenOfNewNode);
            parent.addChild(node);
        }

        for (final Node child : childrenOfNewNode) {
            child.removeParents(parentsOfNewNode);
            child.addParent(node);
        }

        if (node.isInconsistent()) {
            /*
             * This won't be executed with
             * {@link com.github.szgabsz91.morpher.transformationengines.lattice.impl.builders.ConsistentLatticeBuilder}
             * as it only calls {@link Lattice#insertNode(Node)} if the {@link Node} is consistent
             */
            if (this.buildListener != null) {
                node.getParents()
                        .stream()
                        .filter(parent -> !(parent instanceof UnitNode))
                        .forEach(parent -> this.buildListener.onNodeBecomingInconsistent(this, parent));
            }
        }
        else {
            for (int i = 0; i < node.getParents().size(); i++) {
                final Node parent = node.getParents().get(i);

                if (parent instanceof UnitNode) {
                    continue;
                }

                if (!parent.shouldBecomeInconsistentBasedOnDescendant(node)) {
                    continue;
                }

                if (this.buildListener != null &&
                        this.buildListener.onNodeBecomingInconsistent(this, parent)) {
                    i--;
                }
            }
        }

        this.nodeMap.put(node.getPattern(), node);
        if (this.buildListener != null) {
            this.buildListener.onNodeInserted(this, node);
        }
        return true;
    }

    /**
     * Removes the given node from the lattice and makes sure that the links among its parents and children are
     * also updated to reflect this change.
     *
     * @param node the node to be removed
     */
    public void removeNode(final Node node) {
        for (final Node parent : node.getParents()) {
            for (final Node child : node.getChildren()) {
                parent.getChildren().remove(node);
                child.getParents().remove(node);

                if (child.getParents().stream().anyMatch(n -> SubsetCalculator.isSubsetOf(parent, n))) {
                    continue;
                }

                parent.getChildren().add(child);
                child.getParents().add(parent);
            }
        }

        this.nodeMap.remove(node.getPattern());
    }

    /**
     * Fills the dominant rules of the inconsistent nodes in the lattice using
     * {@link LatticeWalker#fillDominantRules()}. If the node levels are still
     * empty, first they are filled.
     */
    public void fillDominantRules() {
        LOGGER.debug("Dominant rule selection phase");
        if (this.getZeroNode().getLevel() == 0L) {
            LOGGER.debug("Filling in node levels");
            this.latticeWalker.fillNodeLevels();
        }
        this.latticeWalker.fillDominantRules();
    }

    /**
     * Fills the node levels.
     */
    public void fillNodeLevels() {
        this.getNodes().forEach(node -> node.setLevel(-1L));
        this.latticeWalker.fillNodeLevels();
    }

    /**
     * Returns the node from the lattice whose rule matches the given list of {@link ICharacter} objects.
     *
     * <p>The search for the target node starts from the unit node and goes down the lattice, always choosing a node
     * from those children the match the characters.</p>
     *
     * <p>If the processing takes place in a node that matches the characters but none of its children match them, the
     * node is returned.</p>
     *
     * <p>Also, if we visit a node that is consistent, the search is over as every other descendant that we might
     * visit will hold the same transformation, so it doesn't make sense to continue the search.</p>
     *
     * @param characters the characters of the word
     * @return the best matching node of the lattice
     */
    public Node match(final List<ICharacter> characters) {
        return latticeWalker.findSingleNode(
                node -> node.getRule().matches(characters),
                Node::isConsistent,
                node -> node.getPartialMatchingFactor(characters)
        );
    }

    /**
     * Returns the node from the lattice whose rule matches the given {@link Word}.
     *
     * <p>The search for the target node starts from the unit node and goes down the lattice, always choosing a node
     * from those children the match the word.</p>
     *
     * <p>If the processing takes place in a node that matches the word but none of its children match that, the
     * node is returned.</p>
     *
     * @param word the word
     * @return the best matching node of the lattice
     */
    public Node match(final Word word) {
        final List<ICharacter> baseCharacters = this.wordConverter.convert(word, characterRepository);
        return this.match(baseCharacters);
    }

    /**
     * Returns the node with the most children.
     * @return the node with the most children
     */
    public Node getNodeWithMostChildren() {
        final Node nodeWithMostChildren = this.getNodes()
                .stream()
                .min((n1, n2) -> Integer.compare(n2.getChildren().size(), n1.getChildren().size()))
                .get();

        if (this.getUnitNode().getChildren().size() >= nodeWithMostChildren.getChildren().size()) {
            return this.getUnitNode();
        }

        return nodeWithMostChildren;
    }

    /**
     * Returns the level of the zero node node, i.e. the length of the maximal path from the unit node to the zero node.
     * @return the level of the zero node node
     */
    public long getZeroNodeLevel() {
        latticeWalker.fillNodeLevels();
        return this.zeroNode.getLevel();
    }

    /**
     * Returns the set of maximal consistent nodes, i.e. the nodes that are consistent but none of its ancestors are.
     * @return the set of maximal consistent nodes
     */
    public Set<Node> getMaximalConsistentNodes() {
        return this.latticeWalker.getMaximalConsistentNodes();
    }

    /**
     * Returns the lattice walker.
     * @return the lattice walker
     */
    public LatticeWalker getLatticeWalker() {
        return latticeWalker;
    }

    /**
     * Sets the lattice walker.
     * @param latticeWalker the lattice walker
     */
    void setLatticeWalker(final LatticeWalker latticeWalker) {
        this.latticeWalker = latticeWalker;
    }

    /**
     * Sets the build listener.
     * @param buildListener the build listener
     */
    public void setBuildListener(final ILatticeBuildListener buildListener) {
        this.buildListener = buildListener;
    }

}
