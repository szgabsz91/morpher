package com.github.szgabsz91.morpher.transformationengines.lattice.impl.trainingsetprocessor.tree;

import com.github.szgabsz91.morpher.transformationengines.api.characters.ICharacter;
import com.github.szgabsz91.morpher.transformationengines.api.characters.attributes.IAttribute;
import com.github.szgabsz91.morpher.transformationengines.api.characters.repositories.ICharacterRepository;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.Addition;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.ITransformation;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.Removal;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.Replacement;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.trainingsetprocessor.costcalculator.ICostCalculator;
import lombok.EqualsAndHashCode;
import lombok.RequiredArgsConstructor;

import java.util.ArrayList;
import java.util.HashSet;
import java.util.List;
import java.util.PriorityQueue;
import java.util.Queue;
import java.util.Set;

/**
 * Tree for word pair processing.
 *
 * @author szgabsz91
 */
@RequiredArgsConstructor
@EqualsAndHashCode
public class WordPairProcessorTree {

    private final WordPairProcessorTreeNode root;
    private final List<ICharacter> leftWordCharacters;
    private final List<ICharacter> rightWordCharacters;
    private final ICharacterRepository characterRepository;
    private final ICostCalculator costCalculator;

    /**
     * Returns the root node of the tree.
     * @return the root node of the tree
     */
    public WordPairProcessorTreeNode getRoot() {
        return root;
    }

    /**
     * Builds the tree according to the given number of paths to process.
     *
     * <p>If the given parameter is 1, all the optimal paths are processed, if it's 2 then all the optimal and the
     * paths with the next best cost are processed, etc.</p>
     *
     * @param pathLevelsToProcess the number of path levels to process
     * @return the processed leaf nodes
     */
    public List<WordPairProcessorTreeNode> build(final int pathLevelsToProcess) {
        final List<WordPairProcessorTreeNode> result = new ArrayList<>();

        int processedPathSize = 0;
        final Queue<WordPairProcessorTreeNode> treeNodesToProcess = new PriorityQueue<>();
        treeNodesToProcess.add(root);
        int previousCost = -1;

        while (!treeNodesToProcess.isEmpty()) {
            final WordPairProcessorTreeNode treeNodeToProcess = treeNodesToProcess.poll();

            if (treeNodeToProcess.getCurrentIndexInStartWord() >= leftWordCharacters.size() &&
                    treeNodeToProcess.getCurrentIndexInEndWord() >= rightWordCharacters.size()) {
                if (treeNodeToProcess.getScoreSoFar() != previousCost) {
                    processedPathSize++;
                    previousCost = treeNodeToProcess.getScoreSoFar();
                }
                if (processedPathSize == pathLevelsToProcess + 1) {
                    break;
                }
                result.add(treeNodeToProcess);
                continue;
            }

            // Replacement
            if (treeNodeToProcess.getCurrentIndexInEndWord() < rightWordCharacters.size() &&
                    treeNodeToProcess.getCurrentIndexInStartWord() < leftWordCharacters.size()) {
                final WordPairProcessorTreeNode middleChild = this.createMiddleChild(treeNodeToProcess);
                treeNodeToProcess.addChild(middleChild);
                treeNodesToProcess.add(middleChild);
            }

            // Addition
            if (treeNodeToProcess.getCurrentIndexInEndWord() < rightWordCharacters.size()) {
                final WordPairProcessorTreeNode leftChild = this.createLeftChild(treeNodeToProcess);
                treeNodeToProcess.addChild(leftChild);
                treeNodesToProcess.add(leftChild);
            }

            // Deletion
            if (treeNodeToProcess.getCurrentIndexInStartWord() < leftWordCharacters.size()) {
                final WordPairProcessorTreeNode rightChild = this.createRightChild(treeNodeToProcess);
                treeNodeToProcess.addChild(rightChild);
                treeNodesToProcess.add(rightChild);
            }
        }

        return result;
    }

    // Addition
    private WordPairProcessorTreeNode createLeftChild(final WordPairProcessorTreeNode parent) {
        final ICharacter rightSide = rightWordCharacters.get(parent.getCurrentIndexInEndWord());
        final Set<? extends IAttribute> attributeSet = new HashSet<>(rightSide.getAttributes());
        final ITransformation transformation = new Addition(attributeSet, characterRepository);

        final int currentIndexInStartWord = parent.getCurrentIndexInStartWord();
        final int currentIndexInEndWord = parent.getCurrentIndexInEndWord() + 1;
        final int scoreSoFar = parent.getScoreSoFar() + costCalculator.calculateCost(transformation);
        final int level = parent.getLevel() + 1;

        return new WordPairProcessorTreeNode(
                currentIndexInStartWord,
                currentIndexInEndWord,
                transformation,
                scoreSoFar,
                level,
                parent
        );
    }

    // Replacement
    private WordPairProcessorTreeNode createMiddleChild(final WordPairProcessorTreeNode parent) {
        final ICharacter leftSide = leftWordCharacters.get(parent.getCurrentIndexInStartWord());
        final ICharacter rightSide = rightWordCharacters.get(parent.getCurrentIndexInEndWord());
        final ITransformation transformation = new Replacement(leftSide, rightSide, characterRepository);

        final int currentIndexInStartWord = parent.getCurrentIndexInStartWord() + 1;
        final int currentIndexInEndWord = parent.getCurrentIndexInEndWord() + 1;
        final int scoreSoFar = parent.getScoreSoFar() + costCalculator.calculateCost(transformation);
        final int level = parent.getLevel() + 1;

        return new WordPairProcessorTreeNode(
                currentIndexInStartWord,
                currentIndexInEndWord,
                transformation,
                scoreSoFar,
                level,
                parent
        );
    }

    // Removal
    private WordPairProcessorTreeNode createRightChild(final WordPairProcessorTreeNode parent) {
        final ICharacter leftSide = leftWordCharacters.get(parent.getCurrentIndexInStartWord());
        final Set<? extends IAttribute> attributeSet = new HashSet<>(leftSide.getAttributes());
        final ITransformation transformation = new Removal(attributeSet, characterRepository);

        final int currentIndexInStartWord = parent.getCurrentIndexInStartWord() + 1;
        final int currentIndexInEndWord = parent.getCurrentIndexInEndWord();
        final int scoreSoFar = parent.getScoreSoFar() + costCalculator.calculateCost(transformation);
        final int level = parent.getLevel();

        return new WordPairProcessorTreeNode(
                currentIndexInStartWord,
                currentIndexInEndWord,
                transformation,
                scoreSoFar,
                level,
                parent
        );
    }

    /**
     * Returns the string representation of this object, i.e. the result of
     * {@link WordPairProcessorTreeNode#toString()}.
     *
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        return root.toString();
    }

}
