package com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations;

import com.github.szgabsz91.morpher.transformationengines.api.characters.ICharacter;
import com.github.szgabsz91.morpher.transformationengines.api.characters.attributes.IAttribute;
import com.github.szgabsz91.morpher.transformationengines.api.characters.repositories.ICharacterRepository;
import lombok.RequiredArgsConstructor;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.util.Collection;
import java.util.HashSet;
import java.util.List;
import java.util.Map;
import java.util.Set;

import static java.util.stream.Collectors.groupingBy;
import static java.util.stream.Collectors.joining;
import static java.util.stream.Collectors.toUnmodifiableSet;

/**
 * {@link ITransformation} implementation class for replacement.
 *
 * @author szgabsz91
 */
@RequiredArgsConstructor
public class Replacement implements ITransformation {

    private static final Logger LOGGER = LoggerFactory.getLogger(Replacement.class);

    private final Set<AttributeDelta<? super IAttribute>> attributeDeltas;
    private final ICharacterRepository characterRepository;

    /**
     * Constructor that computes and sets the attribute deltas so that this replacement transforms the given
     * <code>from</code> character to the given <code>to</code> character.
     *
     * @param from the transformation base
     * @param to the transformation target
     * @param characterRepository the {@link ICharacterRepository} instance
     */
    public Replacement(final ICharacter from, final ICharacter to, final ICharacterRepository characterRepository) {
        this.attributeDeltas = new HashSet<>();
        this.characterRepository = characterRepository;

        final Set<? extends IAttribute> fromAttributes = new HashSet<>(from.getAttributes());
        final Set<? extends IAttribute> toAttributes = new HashSet<>(to.getAttributes());

        final Map<Class<?>, List<IAttribute>> fromAttributeGroups = fromAttributes
                .stream()
                .collect(groupingBy(IAttribute::getClass));
        final Map<Class<?>, List<IAttribute>> toAttributeGroups = toAttributes
                .stream()
                .collect(groupingBy(IAttribute::getClass));
        final Set<Class<?>> fromAttributeTypes = fromAttributeGroups.keySet();
        final Set<Class<?>> toAttributeTypes = toAttributeGroups.keySet();
        final Set<Class<?>> commonAttributeTypes = new HashSet<>(fromAttributeTypes);
        commonAttributeTypes.retainAll(toAttributeTypes);
        final Set<Class<?>> onlyFromAttributeTypes = new HashSet<>(fromAttributeTypes);
        onlyFromAttributeTypes.removeAll(commonAttributeTypes);
        final Set<Class<?>> onlyToAttributeTypes = new HashSet<>(toAttributeTypes);
        onlyToAttributeTypes.removeAll(commonAttributeTypes);

        for (@SuppressWarnings("rawtypes") final Class commonAttributeType : commonAttributeTypes) {
            final IAttribute fromAttribute = fromAttributeGroups.get(commonAttributeType).get(0);
            final IAttribute toAttribute = toAttributeGroups.get(commonAttributeType).get(0);

            if (!fromAttribute.equals(toAttribute)) {
                @SuppressWarnings("unchecked")
                final AttributeDelta<? super IAttribute> attributeDelta = new AttributeDelta<>(
                        commonAttributeType,
                        fromAttribute,
                        toAttribute
                );
                this.attributeDeltas.add(attributeDelta);
            }
        }

        for (@SuppressWarnings("rawtypes") final Class onlyFromAttributeType : onlyFromAttributeTypes) {
            final IAttribute fromAttribute = fromAttributeGroups.get(onlyFromAttributeType).get(0);
            @SuppressWarnings("unchecked")
            final AttributeDelta<? super IAttribute> attributeDelta = AttributeDelta.remove(
                    onlyFromAttributeType,
                    fromAttribute
            );
            this.attributeDeltas.add(attributeDelta);
        }

        for (@SuppressWarnings("rawtypes") final Class onlyToAttributeType : onlyToAttributeTypes) {
            final IAttribute toAttribute = toAttributeGroups.get(onlyToAttributeType).get(0);
            @SuppressWarnings("unchecked")
            final AttributeDelta<? super IAttribute> attributeDelta = AttributeDelta.add(
                    onlyToAttributeType,
                    toAttribute
            );
            this.attributeDeltas.add(attributeDelta);
        }
    }

    /**
     * Returns the attribute deltas.
     * @return the attribute deltas
     */
    public Set<AttributeDelta<? super IAttribute>> getAttributeDeltas() {
        return attributeDeltas;
    }

    /**
     * Returns true if the attribute delta set is empty.
     * @return true if the attribute delta set is empty, false otherwise
     */
    @Override
    public boolean isInconsistent() {
        return this.attributeDeltas.isEmpty();
    }

    /**
     * Performs the replacement, i.e. replaces the attributes in the character from the given list at the given index
     * from the from value to the to value in the attribute delta set.
     *
     * @param characters the list of characters
     * @param index the index at which the replacement must be performed
     * @return the new index, i.e. the given index incremented by one
     */
    @Override
    public int perform(final List<ICharacter> characters, final int index) {
        if (index >= characters.size()) {
            return index;
        }
        final ICharacter fromCharacter = characters.get(index);
        final Map<Class<?>, List<IAttribute>> attributeMap = fromCharacter.getAttributes()
                .stream()
                .collect(groupingBy(IAttribute::getClass));

        for (final AttributeDelta<? super IAttribute> attributeDelta : attributeDeltas) {
            final Class<? extends IAttribute> clazz = attributeDelta.getClazz();
            final IAttribute fromAttribute = fromCharacter.get(clazz);

            if (attributeDelta.getFrom() == null || attributeDelta.getFrom().equals(fromAttribute)) {
                final IAttribute toAttribute = attributeDelta.getTo();

                if (toAttribute != null) {
                    attributeMap.put(clazz, List.of(toAttribute));
                }
                else {
                    attributeMap.remove(clazz);
                }
            }
        }
        final Set<IAttribute> newAttributes = attributeMap.values()
                .stream()
                .flatMap(Collection::stream)
                .collect(toUnmodifiableSet());

        try {
            final ICharacter toCharacter = characterRepository.getCharacter(newAttributes);

            if (characterRepository.getLetter(toCharacter) != null) {
                characters.set(index, toCharacter);
                return index + 1;
            }
        }
        catch (final IllegalArgumentException e) {
            final String errorMessage =
                    "IllegalArgumentException caught from characterRepository for " + newAttributes + " - Original: " +
                    fromCharacter + " and replacement: " + this;
            LOGGER.error(errorMessage, e);
        }

        return index;
    }

    /**
     * Returns if this replacement equals the given other object.
     *
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object other) {
        if (this == other) {
            return true;
        }
        if (other == null || getClass() != other.getClass()) {
            return false;
        }
        final Replacement that = (Replacement) other;
        return attributeDeltas.equals(that.attributeDeltas);
    }

    /**
     * Returns the hash code of this replacement.
     *
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        return attributeDeltas.hashCode();
    }

    /**
     * Returns the string representation of the replacement: either <code>INVARIANT_REPLACEMENT</code> or the
     * attribute deltas converted to string and joined by commas.
     *
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        if (this.attributeDeltas.isEmpty()) {
            return "INVARIANT_REPLACEMENT";
        }

        return this.attributeDeltas
                .stream()
                .map(AttributeDelta::toString)
                .collect(joining(", "));
    }

}
