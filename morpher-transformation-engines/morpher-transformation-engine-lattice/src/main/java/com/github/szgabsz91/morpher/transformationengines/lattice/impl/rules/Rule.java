package com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules;

import com.github.szgabsz91.morpher.core.model.Word;
import com.github.szgabsz91.morpher.transformationengines.api.characters.ICharacter;
import com.github.szgabsz91.morpher.transformationengines.api.characters.repositories.ICharacterRepository;
import com.github.szgabsz91.morpher.transformationengines.api.wordconverters.IWordConverter;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.rules.transformations.ITransformation;
import com.github.szgabsz91.morpher.transformationengines.lattice.impl.setoperations.SubsetCalculator;
import org.apache.commons.collections4.CollectionUtils;

import java.util.ArrayList;
import java.util.List;
import java.util.Objects;

/**
 * Rule implementation class.
 *
 * @author szgabsz91
 */
public class Rule {

    private final Context context;
    private List<ITransformation> transformations;
    private final ICharacterRepository characterRepository;
    private final IWordConverter wordConverter;

    /**
     * Constructor that sets the context and the list of transformations.
     * @param context the context
     * @param transformations the list of transformations
     * @param characterRepository the {@link ICharacterRepository} instance
     * @param wordConverter the {@link IWordConverter} instance
     */
    public Rule(
            final Context context,
            final List<ITransformation> transformations,
            final ICharacterRepository characterRepository,
            final IWordConverter wordConverter) {
        this.context = context;
        this.transformations = transformations;
        this.characterRepository = characterRepository;
        this.wordConverter = wordConverter;
    }

    /**
     * Returns a new identity rule that has an identity context and empty transformation list.
     * @return the identity rule
     */
    public static Rule identity() {
        return new Rule(Context.identity(), List.of(), null, null);
    }

    /**
     * Returns the context.
     * @return the context
     */
    public Context getContext() {
        return context;
    }

    /**
     * Returns the list of transformations.
     * @return the list of transformations
     */
    public List<ITransformation> getTransformations() {
        return transformations;
    }

    /**
     * Sets the list of transformations.
     * @param transformations the list of transformations
     */
    public void setTransformations(final List<ITransformation> transformations) {
        this.transformations = transformations;
    }

    /**
     * Returns whether this rule is identity or not.
     * @return true if the rule is identity, false otherwise
     */
    public boolean isIdentity() {
        return context.isIdentity() && CollectionUtils.isEmpty(transformations);
    }

    /**
     * Returns if the rule matches the given list of characters, i.e. the context fits onto the list somewhere.
     *
     * <p>The changing part is searched from the front of the list.</p>
     *
     * @param characters the list of characters
     * @return true if the rule matches the list of characters, false otherwise
     */
    public boolean matchesFromFront(final List<ICharacter> characters) {
        if (this.isIdentity()) {
            return false;
        }

        final ContextIndexWrapper contextIndexWrapper = this.getIndexFromFront(characters);
        final int index = contextIndexWrapper.getIndex();
        final List<ICharacter> contextCharacters = contextIndexWrapper.getContextCharacters();

        final int startIndex = index - this.context.getPrefix().size() +
                (this.context.getPrefix().get(0).isStart() ? 1 : 0);

        if (startIndex < 0) {
            return false;
        }

        if (startIndex == 0 && contextCharacters.get(startIndex).isStart() && !characters.get(startIndex).isStart()) {
            contextCharacters.remove(0);
        }

        for (int i = 0; i < Math.min(contextCharacters.size(), characters.size()); i++) {
            final ICharacter character = characters.get(startIndex + i);
            final ICharacter reference = contextCharacters.get(i);

            if (!SubsetCalculator.isSubsetOf(reference, character)) {
                return false;
            }
        }

        return true;
    }

    /**
     * Returns if the rule matches the given list of characters, i.e. the context fits onto the list somewhere.
     *
     * <p>The changing part is searched from the back of the list.</p>
     *
     * @param characters the list of characters
     * @return true if the rule matches the list of characters, false otherwise
     */
    public boolean matchesFromBack(final List<ICharacter> characters) {
        if (this.isIdentity()) {
            return false;
        }

        final ContextIndexWrapper contextIndexWrapper = this.getIndexFromBack(characters);
        final int index = contextIndexWrapper.getIndex();
        final List<ICharacter> contextCharacters = contextIndexWrapper.getContextCharacters();

        final int startIndex = index - this.context.getPrefix().size() +
                (!this.context.getPrefix().isEmpty() && this.context.getPrefix().get(0).isStart() ? 1 : 0);
        final int correction =
                !this.context.getPrefix().isEmpty() && this.context.getPrefix().get(0).isStart() ? 1 : 0;

        if (startIndex < 0) {
            return false;
        }

        int maximumIndex = Math.min(contextCharacters.size(), characters.size());

        // Check if the rule's context is longer than the word
        if (maximumIndex + 1 < contextCharacters.size()) {
            final List<ICharacter> contextEnd = contextCharacters.subList(maximumIndex + 1, contextCharacters.size());
            if (!contextEnd.get(0).isEnd()) {
                return false;
            }
        }

        // Regression: middle changes
        // Check if the maximum index is larger than the last context character we will check
        if (maximumIndex - 1 + correction >= contextCharacters.size()) {
            maximumIndex = contextCharacters.size() - correction;
        }

        for (int i = 0; i < maximumIndex; i++) {
            try {
                final ICharacter character = characters.get(startIndex + i);
                final ICharacter reference = contextCharacters.get(i + correction);

                if (!SubsetCalculator.isSubsetOf(reference, character)) {
                    return false;
                }
            }
            catch (final IndexOutOfBoundsException e) {
                final ICharacter reference = contextCharacters.get(i + correction);

                if (!reference.isEnd()) {
                    return false;
                }

                break;
            }
        }

        return true;
    }

    /**
     * Returns if the rule matches the given list of characters, i.e. the context fits onto the list somewhere.
     *
     * @param characters the list of characters
     * @return true if the rule matches the list of characters, false otherwise
     */
    public boolean matches(final List<ICharacter> characters) {
        if (this.isIdentity()) {
            return false;
        }

        final Position frontPosition = context.getFrontPosition();
        final Position backPosition = context.getBackPosition();

        if (frontPosition == null) {
            return this.matchesFromBack(characters);
        }

        if (backPosition == null) {
            return this.matchesFromFront(characters);
        }

        return this.matchesFromBack(characters);
    }

    /**
     * Returns if the rule matches the given word, i.e. the context fits onto the list of characters somewhere.
     *
     * @param word the word
     * @return true if the rule matches the word, false otherwise
     */
    public boolean matches(final Word word) {
        if (word == null || this.isIdentity()) {
            return false;
        }

        final List<ICharacter> characters = this.wordConverter.convert(word, characterRepository);
        return matches(characters);
    }

    /**
     * Transforms the given word according to the rule components, searching for the changing part from the front of
     * the word.
     *
     * @param word the input word
     * @return the resulting word
     */
    public Word transformFromFront(final Word word) {
        if (this.isIdentity() || CollectionUtils.isEmpty(this.transformations)) {
            return word;
        }

        final List<ICharacter> characters = this.wordConverter.convert(word, characterRepository);
        final ContextIndexWrapper contextIndexWrapper = this.getIndexFromFront(characters);
        int index = contextIndexWrapper.getIndex();

        for (final ITransformation transformation : transformations) {
            index = transformation.perform(characters, index);
        }

        return this.wordConverter.convertBack(characters, characterRepository);
    }

    /**
     * Transforms the given word according to the rule components, searching for the changing part from the back of
     * the word.
     *
     * @param word the input word
     * @return the resulting word
     */
    public Word transformFromBack(final Word word) {
        if (this.isIdentity() || CollectionUtils.isEmpty(this.transformations)) {
            return word;
        }

        final List<ICharacter> characters = this.wordConverter.convert(word, characterRepository);
        final ContextIndexWrapper contextIndexWrapper = this.getIndexFromBack(characters);
        int index = contextIndexWrapper.getIndex();

        for (final ITransformation transformation : transformations) {
            index = transformation.perform(characters, index);
        }

        return this.wordConverter.convertBack(characters, characterRepository);
    }

    /**
     * Transforms the given word according to the rule components.
     *
     * @param word the input word
     * @return the resulting word
     */
    public Word transform(final Word word) {
        if (this.isIdentity() || CollectionUtils.isEmpty(this.transformations)) {
            return word;
        }

        final Position frontPosition = context.getFrontPosition();
        final Position backPosition = context.getBackPosition();

        if (frontPosition == null) {
            return this.transformFromBack(word);
        }

        if (backPosition == null) {
            return this.transformFromFront(word);
        }

        final List<ICharacter> postfix = context.getPostfix();

        if (!postfix.isEmpty() && postfix.get(postfix.size() - 1).isEnd()) {
            return this.transformFromBack(word);
        }

        return this.transformFromBack(word);
    }

    private ContextIndexWrapper getIndexFromFront(final List<ICharacter> word) {
        final ContextIndexWrapper contextIndexWrapper = new ContextIndexWrapper(this.context, word);

        if (contextIndexWrapper.getIndex() != null) {
            return contextIndexWrapper;
        }

        final List<ICharacter> contextCharacters = contextIndexWrapper.getContextCharacters();
        final Position frontPosition = context.getFrontPosition();

        int result = -1;
        int matchCounter = -1;

        for (int i = 0; i <= word.size() - contextCharacters.size(); i++) {
            final List<ICharacter> sublist = word.subList(i, i + contextCharacters.size());

            if (characterListsMatch(contextCharacters, sublist)) {
                matchCounter++;
                result = i;
            }

            if (matchCounter == frontPosition.getIndex()) {
                break;
            }
        }

        contextIndexWrapper.setIndex(result + this.context.getPrefix().size());
        return contextIndexWrapper;
    }

    private ContextIndexWrapper getIndexFromBack(final List<ICharacter> word) {
        final ContextIndexWrapper contextIndexWrapper = new ContextIndexWrapper(this.context, word);

        if (contextIndexWrapper.getIndex() != null) {
            return contextIndexWrapper;
        }

        final List<ICharacter> contextCharacters = contextIndexWrapper.getContextCharacters();
        final Position backPosition = context.getBackPosition();

        int result = -1;
        int matchCounter = -1;

        for (int i = word.size() - contextCharacters.size(); i >= 0; i--) {
            final List<ICharacter> sublist = word.subList(i, i + contextCharacters.size());

            if (characterListsMatch(contextCharacters, sublist)) {
                matchCounter++;
            }

            if (matchCounter == backPosition.getIndex()) {
                result = i;
                break;
            }
        }

        result += this.context.getPrefix().size();

        if (result < 0) {
            final int correction = contextCharacters.get(contextCharacters.size() - 1).isEnd() ? 1 : 0;
            result = word.size() - contextCharacters.size() + correction;
        }

        contextIndexWrapper.setIndex(result);
        return contextIndexWrapper;
    }

    private static boolean characterListsMatch(final List<ICharacter> intersection, final List<ICharacter> list) {
        for (int i = 0; i < intersection.size(); i++) {
            final ICharacter intersectionCharacter = intersection.get(i);
            final ICharacter listCharacter = list.get(i);

            if (!SubsetCalculator.isSubsetOf(intersectionCharacter, listCharacter)) {
                return false;
            }
        }

        return true;
    }

    /**
     * Returns if this rule equals the given other object.
     *
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object other) {
        if (this == other) {
            return true;
        }
        if (other == null || getClass() != other.getClass()) {
            return false;
        }
        final Rule rule = (Rule) other;
        return context.equals(rule.context) &&
                Objects.equals(transformations, rule.transformations);
    }

    /**
     * Returns the hash code of this rule.
     *
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        int result = context.hashCode();
        result = 31 * result + (transformations != null ? transformations.hashCode() : 0);
        return result;
    }

    /**
     * Returns the string representation of this rule.
     *
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        return "Rule[" + context + ", " + transformations + "]";
    }

    /**
     * Inner class for holding context characters and an index.
     *
     * @author szgabsz91
     */
    private static final class ContextIndexWrapper {

        private final List<ICharacter> contextCharacters;
        private Integer index;

        private ContextIndexWrapper(final Context context, final List<ICharacter> word) {
            this.contextCharacters = new ArrayList<>(
                    context.getPrefix().size() + context.getCore().size() + context.getPostfix().size()
            );
            this.contextCharacters.addAll(context.getPrefix());
            this.contextCharacters.addAll(context.getCore());
            this.contextCharacters.addAll(context.getPostfix());

            if (contextCharacters.get(0).isStart()) {
                this.index = context.getPrefix().size() - 1;
            }
            else if (contextCharacters.get(contextCharacters.size() - 1).isEnd()) {
                this.index = word.size() - context.getPostfix().size() - context.getCore().size() + 1;
            }
            else if (contextCharacters.size() > word.size()) {
                this.index = -1;
            }

            if (this.index != null && this.index < 0) {
                this.index = 0;
            }
        }

        private List<ICharacter> getContextCharacters() {
            return contextCharacters;
        }

        private Integer getIndex() {
            return index;
        }

        private void setIndex(final int index) {
            this.index = index;
        }

    }

}
