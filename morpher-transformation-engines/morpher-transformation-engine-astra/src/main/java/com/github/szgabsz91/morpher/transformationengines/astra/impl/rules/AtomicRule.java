package com.github.szgabsz91.morpher.transformationengines.astra.impl.rules;

import com.github.szgabsz91.morpher.transformationengines.api.characters.attributes.Letter;

import java.util.List;

import static java.util.stream.Collectors.joining;

/**
 * Class for representing an atomic rule that has a context (prefix, changing part and postfix), and a changing and
 * changed part.
 *
 * @author szgabsz91
 */
public class AtomicRule {

    private final String prefix;
    private final String changingSubstring;
    private final String replacementString;
    private final String postfix;
    private final String _context;
    private int support;
    private int wordFrequency;

    /**
     * Constructor that sets all the fields.
     * @param prefix the prefix of the atomic rule
     * @param changingSubstring the changing substring of the atomic rule
     * @param replacementString the replacement string of the atomic rule
     * @param postfix the postfix of the atomic rule
     * @param support the support of the atomic rule
     * @param wordFrequency the sum of training word frequencies that this atomic rule matches
     */
    public AtomicRule(
            final String prefix,
            final String changingSubstring,
            final String replacementString,
            final String postfix,
            final int support,
            final int wordFrequency) {
        this.prefix = prefix;
        this.changingSubstring = changingSubstring;
        this.replacementString = replacementString;
        this.postfix = postfix;
        this._context = prefix + changingSubstring + postfix;
        this.support = support;
        this.wordFrequency = wordFrequency;
    }

    /**
     * Constructor that sets all the fields, including the support to one.
     * @param prefix the prefix of the atomic rule
     * @param changingSubstring the changing substring of the atomic rule
     * @param replacementString the replacement string of the atomic rule
     * @param postfix the postfix of the atomic rule
     * @param wordFrequency the sum of training word frequencies that this atomic rule matches
     */
    public AtomicRule(
            final String prefix,
            final String changingSubstring,
            final String replacementString,
            final String postfix,
            final int wordFrequency) {
        this(prefix, changingSubstring, replacementString, postfix, 1, wordFrequency);
    }

    /**
     * Returns the prefix of this atomic rule.
     * @return the prefix of this atomic rule
     */
    public String getPrefix() {
        return prefix;
    }

    /**
     * Returns the changing substring of this atomic rule.
     * @return the changing substring of this atomic rule
     */
    public String getChangingSubstring() {
        return changingSubstring;
    }

    /**
     * Returns the replacement string of this atomic rule.
     * @return the replacement string of this atomic rule
     */
    public String getReplacementString() {
        return replacementString;
    }

    /**
     * Returns the postfix of this atomic rule.
     * @return the postfix of this atomic rule
     */
    public String getPostfix() {
        return postfix;
    }

    /**
     * Returns the context of this atomic rule.
     * @return the context of this atomic rule
     */
    public String getContext() {
        return _context;
    }

    /**
     * Returns the support value of the atomic rule, i.e. how many training word pairs match it.
     * @return the support value of the atomic rule, i.e. how many training word pairs match it
     */
    public int getSupport() {
        return support;
    }

    /**
     * Returns the word frequency.
     * @return the word frequency
     */
    public int getWordFrequency() {
        return wordFrequency;
    }

    /**
     * Returns the multiplication of the support and the word frequency.
     * @return the multiplication of the support and the word frequency
     */
    public int getAggregatedSupport() {
        return support * wordFrequency;
    }

    /**
     * Merges the given {@link AtomicRule} into this one by incrementing the support by one and the word frequency by
     * the word frequency of the given {@link AtomicRule}.
     *
     * @param otherAtomicRule the other {@link AtomicRule} to merge
     */
    public void merge(final AtomicRule otherAtomicRule) {
        this.support++;
        this.wordFrequency += otherAtomicRule.getWordFrequency();
    }

    /**
     * Sets the support to the support sum of the given list, and the word frequency to the word frequency sum
     * of the given list.
     *
     * @param atomicRules the list of {@link AtomicRule}s
     */
    public void setSupportAndWordFrequency(final List<AtomicRule> atomicRules) {
        this.support = atomicRules
                .stream()
                .mapToInt(AtomicRule::getSupport)
                .sum();
        this.wordFrequency = atomicRules
                .stream()
                .mapToInt(AtomicRule::getWordFrequency)
                .sum();
    }

    /**
     * Reverses the atomic rule.
     * @return the reversed atomic rule
     */
    public AtomicRule reverse() {
        return new AtomicRule(
                reverse(postfix),
                reverse(changingSubstring),
                reverse(replacementString),
                reverse(prefix),
                support,
                wordFrequency
        );
    }

    /**
     * Similar to {@link AtomicRule#equals(Object)} but also compares the support values.
     * @param other the other object
     * @return true if the two objects equals, false otherwise
     */
    public boolean fullEquals(final Object other) {
        return this.equals(other) &&
                this.support == ((AtomicRule) other).support &&
                this.wordFrequency == ((AtomicRule) other).wordFrequency;
    }

    /**
     * Returns if this object equals the given other object.
     *
     * {@inheritDoc}
     */
    @Override
    public boolean equals(final Object other) {
        if (this == other) {
            return true;
        }
        if (other == null || getClass() != other.getClass()) {
            return false;
        }
        final AtomicRule that = (AtomicRule) other;
        return prefix.equals(that.prefix) &&
                changingSubstring.equals(that.changingSubstring) &&
                replacementString.equals(that.replacementString) &&
                postfix.equals(that.postfix);
    }

    /**
     * Returns the hash code of this object.
     *
     * {@inheritDoc}
     */
    @Override
    public int hashCode() {
        int result = prefix.hashCode();
        result = 31 * result + changingSubstring.hashCode();
        result = 31 * result + replacementString.hashCode();
        result = 31 * result + postfix.hashCode();
        return result;
    }

    /**
     * Returns the string representation of this object.
     *
     * {@inheritDoc}
     */
    @Override
    public String toString() {
        return "AtomicRule[" +
                prefix + ", " +
                changingSubstring + ", " +
                replacementString + ", " +
                postfix + ", " +
                support + ", " +
                wordFrequency +
                "]";
    }

    private static String reverse(final String string) {
        return new StringBuilder(string)
                .reverse()
                .toString()
                .chars()
                .mapToObj(character -> (char) character)
                .map(character -> {
                    if (character == Letter.START_SYMBOL.toString().charAt(0)) {
                        return Letter.END_SYMBOL.toString();
                    }

                    if (character == Letter.END_SYMBOL.toString().charAt(0)) {
                        return Letter.START_SYMBOL.toString();
                    }

                    return Character.toString(character);
                })
                .collect(joining());
    }

}
