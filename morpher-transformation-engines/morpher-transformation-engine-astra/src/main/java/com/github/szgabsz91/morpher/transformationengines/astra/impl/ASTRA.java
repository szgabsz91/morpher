    /**
     * Adds the given atomic rules to the rule base.
     * @param atomicRules the atomic rules to add
     */
    void addAtomicRules(final List<AtomicRule> atomicRules) {
        Stream<AtomicRule> atomicRuleStream = atomicRules
                .stream()
                .collect(groupingBy(atomicRule -> atomicRule, toList()))
                .entrySet()
                .stream()
                .map(entry -> {
                    final AtomicRule atomicRule = entry.getKey();
                    final List<AtomicRule> atomicRuleList = entry.getValue();
                    atomicRule.setSupportAndWordFrequency(atomicRuleList);
                    return atomicRule;
                });
        if (this.minimumSupportThreshold != null) {
            atomicRuleStream = atomicRuleStream
                    .filter(atomicRule -> atomicRule.getSupport() > this.minimumSupportThreshold);
        }
        if (this.minimumWordFrequencyThreshold != null) {
            atomicRuleStream = atomicRuleStream
                    .filter(atomicRule -> atomicRule.getWordFrequency() > this.minimumWordFrequencyThreshold);
        }
        if (this.minimumAggregatedSupportThreshold != null) {
            atomicRuleStream = atomicRuleStream
                    .filter(atomicRule -> atomicRule.getAggregatedSupport() > this.minimumAggregatedSupportThreshold);
        }
        final Set<AtomicRule> atomicRuleSet = atomicRuleStream
                .collect(toSet());
        this.searcher.addAtomicRules(atomicRuleSet);
    }

    /**
     * Learns the given set of word pairs.
     *
     * {@inheritDoc}
     */
    @Override
    public void learnWordPairs(final Set<FrequencyAwareWordPair> wordPairs) {
        final List<AtomicRule> atomicRules = wordPairs
                .stream()
                .flatMap(wordPair -> {
                    Stream<AtomicRule> stream = generateAtomicRulesReversed(wordPair);

                    if (stream != null) {
                        return stream;
                    }

                    stream = generateAtomicRulesCompound(wordPair);

                    if (stream != null) {
                        return stream;
                    }

                    final String startCharacter = this.characterRepository.getStartCharacter().toString();
                    final String endCharacter = this.characterRepository.getEndCharacter().toString();
                    final Word leftWord = wordPair.getLeftWord();
                    final Word rightWord = wordPair.getRightWord();
                    return generateAtomicRules(FrequencyAwareWordPair.of(
                            startCharacter + leftWord + endCharacter,
                            startCharacter + rightWord + endCharacter,
                            wordPair.getFrequency()
                    ));
                })
                .collect(toList());
        this.addAtomicRules(atomicRules);
    }

    private Stream<AtomicRule> generateAtomicRulesReversed(final FrequencyAwareWordPair wordPair) {
        Word leftWord = wordPair.getLeftWord();
        Word rightWord = wordPair.getRightWord();

        if (!rightWord.endsWith(leftWord)) {
            return null;
        }

        final String startCharacter = this.characterRepository.getStartCharacter().toString();
        final String endCharacter = this.characterRepository.getEndCharacter().toString();

        leftWord = leftWord
                .reverse()
                .replace(startCharacter, endCharacter)
                .replace(endCharacter, startCharacter);
        rightWord = rightWord
                .reverse()
                .replace(startCharacter, endCharacter)
                .replace(endCharacter, startCharacter);

        final FrequencyAwareWordPair extendedWordPair = FrequencyAwareWordPair.of(
                startCharacter + leftWord + endCharacter,
                startCharacter + rightWord + endCharacter,
                wordPair.getFrequency()
        );
        return generateAtomicRules(extendedWordPair)
                .map(AtomicRule::reverse);
    }

    private Stream<AtomicRule> generateAtomicRulesCompound(final FrequencyAwareWordPair wordPair) {
        final Word leftWord = wordPair.getLeftWord();
        final Word rightWord = wordPair.getRightWord();
        final String leftWordString = leftWord.toString();
        final String rightWordString = rightWord.toString();
        final int changingSubstringIndex = rightWordString.indexOf(leftWordString);
        if (changingSubstringIndex == -1) {
            return null;
        }
        final String prefix = rightWordString.substring(0, changingSubstringIndex);
        final int replacementStringIndex = changingSubstringIndex + leftWordString.length();
        final String suffix = rightWordString.substring(replacementStringIndex);
        final String startCharacter = this.characterRepository.getStartCharacter().toString();
        final String endCharacter = this.characterRepository.getEndCharacter().toString();
        final FrequencyAwareWordPair prefixWordPair = FrequencyAwareWordPair.of(
                leftWordString,
                prefix + leftWordString,
                wordPair.getFrequency()
        );
        final FrequencyAwareWordPair suffixWordPair = FrequencyAwareWordPair.of(
                startCharacter + leftWordString + endCharacter,
                startCharacter + leftWordString + suffix + endCharacter,
                wordPair.getFrequency()
        );
        final Stream<AtomicRule> prefixAtomicRules = generateAtomicRulesReversed(prefixWordPair);
        if (prefixAtomicRules == null) {
            return null;
        }
        final Stream<AtomicRule> suffixAtomicRules = generateAtomicRules(suffixWordPair);
        return Stream.concat(prefixAtomicRules, suffixAtomicRules);
    }

    /**
     * Transforms the given input word forwards.
     *
     * {@inheritDoc}
     */
    @Override
    public Optional<TransformationEngineResponse> transform(final Word input) {
        final Word convertedWord = this.wordConverter.convert(input);
        final String inputWord = this.characterRepository.getStartCharacter().toString() +
                convertedWord.toString() +
                this.characterRepository.getEndCharacter().toString();
        final AtomicRuleCandidate[] atomicRuleCandidates = filterAtomicRuleCandidates(
                this.searcher.searchAtomicRuleCandidatesForForwardsTransformation(inputWord)
        );
        return this.processAtomicRuleCandidates(inputWord, atomicRuleCandidates);
    }

    /**
     * Transforms the given input word backwards.
     *
     * {@inheritDoc}
     */
    @Override
    public Optional<TransformationEngineResponse> transformBack(final Word input) {
        final Word convertedWord = this.wordConverter.convert(input);
        final String inputWord = this.characterRepository.getStartCharacter().toString() +
                convertedWord.toString() +
                this.characterRepository.getEndCharacter().toString();
        final AtomicRuleCandidate[] atomicRuleCandidates = filterAtomicRuleCandidates(
                this.searcher.searchAtomicRuleCandidatesForBackwardsTransformation(inputWord)
        );
        return this.processAtomicRuleCandidates(inputWord, atomicRuleCandidates);
    }

    private AtomicRuleCandidate[] filterAtomicRuleCandidates(final AtomicRuleCandidate[] atomicRuleCandidates) {
        if (atomicRuleCandidates.length == 0) {
            return atomicRuleCandidates;
        }

        AtomicRuleCandidate[] filteredAtomicRuleCandidates = Arrays.stream(atomicRuleCandidates)
                .filter(atomicRuleCandidate -> atomicRuleCandidate.getFitness() == 1.0)
                .toArray(AtomicRuleCandidate[]::new);

        if (filteredAtomicRuleCandidates.length == 0) {
            filteredAtomicRuleCandidates = atomicRuleCandidates;
        }

        filteredAtomicRuleCandidates = Arrays.stream(filteredAtomicRuleCandidates)
                .filter(atomicRuleCandidate -> atomicRuleCandidate.getFitness() >= this.fitnessThreshold)
                .toArray(AtomicRuleCandidate[]::new);

        if (filteredAtomicRuleCandidates.length == 0) {
            filteredAtomicRuleCandidates = atomicRuleCandidates;
        }

        final Set<AtomicRuleCandidate> resultingSet = new HashSet<>(Arrays.asList(filteredAtomicRuleCandidates));
        final IAtomicRuleComponentAccessor componentAccessor = atomicRuleCandidates[0].getAtomicRuleComponentAccessor();

        return Arrays.stream(filteredAtomicRuleCandidates)
                .filter(atomicRuleCandidate -> {
                    final AtomicRule atomicRule = atomicRuleCandidate.getAtomicRule();
                    final String context = componentAccessor.getContext(atomicRule);
                    final String changingSubstring = componentAccessor.getChangingSubstring(atomicRule);
                    final String replacementString = componentAccessor.getReplacementString(atomicRule);

                    return resultingSet
                            .stream()
                            .noneMatch(existing -> {
                                if (existing == atomicRuleCandidate) {
                                    return false;
                                }

                                final AtomicRule existingAtomicRule = existing.getAtomicRule();
                                final String existingChangingSubstring =
                                        componentAccessor.getChangingSubstring(existingAtomicRule);
                                if (!existingChangingSubstring.equals(changingSubstring)) {
                                    return false;
                                }

                                final String existingReplacementString =
                                        componentAccessor.getReplacementString(existingAtomicRule);
                                if (!existingReplacementString.equals(replacementString)) {
                                    return false;
                                }

                                final String existingContext = componentAccessor.getContext(existingAtomicRule);
                                return existingContext.contains(context);
                            });
                })
                .toArray(AtomicRuleCandidate[]::new);
    }

    private Optional<TransformationEngineResponse> processAtomicRuleCandidates(
            final String inputWord,
            final AtomicRuleCandidate[] atomicRuleCandidates) {
        if (atomicRuleCandidates.length == 0) {
            return Optional.empty();
        }

        final Set<ApplicableAtomicRule> applicableAtomicRuleSet = new LinkedHashSet<>();

        for (int index = 0; index < atomicRuleCandidates.length; index++) {
            final AtomicRuleCandidate atomicRuleCandidate = atomicRuleCandidates[index];
            final double fitness = atomicRuleCandidate.getFitness();
            final IAtomicRuleComponentAccessor atomicRuleComponentAccessor =
                    atomicRuleCandidate.getAtomicRuleComponentAccessor();
            final AtomicRule atomicRule = atomicRuleCandidate.getAtomicRule();
            final String context = atomicRuleComponentAccessor.getContext(atomicRule);
            final String prefix = atomicRuleComponentAccessor.getPrefix(atomicRule);
            final String changingSubstring = atomicRuleComponentAccessor.getChangingSubstring(atomicRule);
            final int leftIndex = inputWord.indexOf(context) + prefix.length();
            final int rightIndex = inputWord.indexOf(context) + prefix.length() + changingSubstring.length() - 1;
            final ApplicableAtomicRule newApplicableAtomicRule = new ApplicableAtomicRule(
                    atomicRuleComponentAccessor,
                    atomicRule,
                    fitness,
                    leftIndex,
                    rightIndex
            );
            applicableAtomicRuleSet.add(newApplicableAtomicRule);
        }

        if (applicableAtomicRuleSet.isEmpty()) {
            return Optional.empty();
        }

        final List<RuleBucket> ruleBuckets = this.createRuleBuckets(applicableAtomicRuleSet);

        final List<ProbabilisticWord> probabilisticWords = IntStream.range(1, ruleBuckets.size() + 1)
                .boxed()
                .map(size -> {
                    return Generator.combination(ruleBuckets)
                            .simple(size)
                            .stream()
                            .collect(toList());
                })
                .flatMap(ruleBucketCombinations -> {
                    return ruleBucketCombinations
                            .stream()
                            .flatMap(ruleBucketCombination -> {
                                @SuppressWarnings("unchecked")
                                final List<ApplicableAtomicRule>[] atomicRulesCombination = ruleBucketCombination
                                        .stream()
                                        .map(RuleBucket::getApplicableAtomicRules)
                                        .toArray(List[]::new);

                                return Generator.cartesianProduct(atomicRulesCombination)
                                        .stream()
                                        .map(applicableAtomicRules -> this.apply(inputWord, applicableAtomicRules));
                            });
                })
                .filter(Objects::nonNull)
                .collect(groupingBy(ProbabilisticWord::getWord))
                .entrySet()
                .stream()
                .map(entry -> {
                    final Word word = this.wordConverter.convertBack(entry.getKey());
                    final double probability = entry.getValue()
                            .stream()
                            .sorted()
                            .findFirst()
                            .map(ProbabilisticWord::getProbability)
                            .orElse(0.0);
                    return ProbabilisticWord.of(word, probability);
                })
                .filter(probabilisticWord -> probabilisticWord.getProbability() > 0.0)
                .sorted()
                .limit(this.maximumNumberOfResponses)
                .collect(toList());

        final List<ProbabilisticWord> filteredProbabilisticWords = filterProbabilisticWords(probabilisticWords);
        final TransformationEngineResponse transformationEngineResponse =
                TransformationEngineResponse.of(filteredProbabilisticWords);
        return Optional.of(transformationEngineResponse);
    }

    private Stream<AtomicRule> generateAtomicRules(final FrequencyAwareWordPair wordPair) {
        final List<Segment> segments = new ArrayList<>();

        final FrequencyAwareWordPair convertedWordPair = this.wordConverter.convert(wordPair);
        final String leftWord = convertedWordPair.getLeftWord().toString();
        final String rightWord = convertedWordPair.getRightWord().toString();
        int rightWordLowerBound = 0;

        for (int leftWordIndex = 0; leftWordIndex < leftWord.length(); leftWordIndex++) {
            for (int rightWordIndex = rightWordLowerBound; rightWordIndex < rightWord.length(); rightWordIndex++) {
                while (rightWordIndex > leftWordIndex && leftWordIndex + 1 < leftWord.length()) {
                    leftWordIndex++;
                }

                final int upperIndexBound = Math.min(
                        leftWord.length() - leftWordIndex,
                        rightWord.length() - rightWordIndex
                );

                final int[] lengths = IntStream.rangeClosed(1, upperIndexBound).toArray();
                final List<Segment> segmentCandidates = new ArrayList<>(lengths.length);

                for (final int length : lengths) {
                    final int leftWordUpperIndex = Math.min(
                            leftWordIndex + length,
                            leftWord.length()
                    );
                    final int rightWordUpperIndex = Math.min(
                            rightWordIndex + length,
                            rightWord.length()
                    );
                    final String leftWordSubstring = leftWord.substring(leftWordIndex, leftWordUpperIndex);
                    final String rightWordSubstring = rightWord.substring(rightWordIndex, rightWordUpperIndex);
                    final Segment segment = new Segment(
                            leftWordIndex,
                            rightWordIndex,
                            leftWordSubstring,
                            rightWordSubstring
                    );

                    if (segment.isVariant()) {
                        break;
                    }

                    if (segment.getLeftWordSubstring().length() >= minimalMatchingSegmentLength) {
                        final double segmentFitness = this.segmentFitnessCalculator.calculate(segment);
                        segment.setFitness(segmentFitness);
                        segmentCandidates.add(segment);
                    }
                }

                if (!segmentCandidates.isEmpty()) {
                    Collections.sort(segmentCandidates);
                    final Segment chosenSegment = segmentCandidates.get(0);

                    leftWordIndex += chosenSegment.getLeftWordSubstring().length() - 1;
                    rightWordLowerBound += chosenSegment.getRightWordSubstring().length();

                    if (segments.isEmpty()) {
                        if (chosenSegment.getLeftWordIndex() > 0) {
                            final int leftWordStartIndex = 0;
                            final int rightWordStartIndex = chosenSegment.getRightWordIndex() - 1;
                            final String leftWordSubstring = leftWord.substring(0, chosenSegment.getLeftWordIndex());
                            final String rightWordSubstring = rightWord.substring(0, chosenSegment.getRightWordIndex());
                            final Segment variantSegment = new Segment(
                                    leftWordStartIndex,
                                    rightWordStartIndex,
                                    leftWordSubstring,
                                    rightWordSubstring
                            );
                            segments.add(variantSegment);
                        }
                    }
                    else {
                        final Segment previousSegment = segments.get(segments.size() - 1);
                        final int previousLeftWordIndex = previousSegment.getLeftWordIndex() +
                                previousSegment.getLeftWordSubstring().length() -
                                1;
                        final int previousRightWordIndex = previousSegment.getRightWordIndex() +
                                previousSegment.getRightWordSubstring().length() -
                                1;
                        final int currentLeftWordIndex = chosenSegment.getLeftWordIndex();
                        final int currentRightWordIndex = chosenSegment.getRightWordIndex();
                        String leftWordSubstring = "";
                        String rightWordSubstring = "";
                        if (currentLeftWordIndex != previousLeftWordIndex + 1) {
                            leftWordSubstring = leftWord.substring(previousLeftWordIndex + 1, currentLeftWordIndex);
                        }
                        if (currentRightWordIndex != previousRightWordIndex + 1) {
                            rightWordSubstring = rightWord.substring(
                                    Math.min(previousRightWordIndex + 1, currentRightWordIndex),
                                    currentRightWordIndex
                            );
                        }
                        final Segment segment = new Segment(
                                previousLeftWordIndex + 1,
                                previousRightWordIndex + 1,
                                leftWordSubstring,
                                rightWordSubstring
                        );
                        segments.add(segment);
                    }

                    segments.add(chosenSegment);
                    break;
                }
            }
        }

        if (segments.isEmpty()) {
            final Segment segment = new Segment(0, 0, leftWord, rightWord);
            segments.add(segment);
        }
        else {
            final Segment lastSegment = segments.get(segments.size() - 1);
            final int lastLeftWordIndex = lastSegment.getLeftWordIndex() + lastSegment.getLeftWordSubstring().length();
            final int lastRightWordIndex = lastSegment.getRightWordIndex() +
                    lastSegment.getRightWordSubstring().length();

            if (lastLeftWordIndex < leftWord.length() - 1 || lastRightWordIndex < rightWord.length() - 1) {
                final String leftWordSubstring = leftWord.substring(lastLeftWordIndex);
                final String rightWordSubstring = rightWord.substring(lastRightWordIndex);
                final Segment segment = new Segment(
                        lastLeftWordIndex,
                        lastRightWordIndex,
                        leftWordSubstring,
                        rightWordSubstring
                );
                segments.add(segment);
            }
        }

        return segments
                .stream()
                .filter(Segment::isVariant)
                .flatMap(segment -> createAtomicRules(segment, convertedWordPair));
    }

    private Stream<AtomicRule> createAtomicRules(final Segment segment, final FrequencyAwareWordPair wordPair) {
        final String leftWord = wordPair.getLeftWord().toString();
        final int leftStartIndex = 0;
        final int leftEndIndex = segment.getLeftWordIndex() - 1;
        final int rightStartIndex = segment.getLeftWordIndex() + segment.getLeftWordSubstring().length();
        final int rightEndIndex = leftWord.length() - 1;
        final int leftIndexDifference = leftEndIndex - leftStartIndex + 1;
        final int rightIndexDifference = rightEndIndex - rightStartIndex + 1;
        final int maximumIndexDifference = Math.max(leftIndexDifference, rightIndexDifference);
        final AtomicRule coreAtomicRule = new AtomicRule(
                "",
                segment.getLeftWordSubstring(),
                segment.getRightWordSubstring(),
                "",
                wordPair.getFrequency()
        );

        Stream<AtomicRule> atomicRuleStream = IntStream.rangeClosed(0, maximumIndexDifference)
                .mapToObj(index -> {
                    if (index == 0) {
                        return coreAtomicRule;
                    }

                    final String prefix = leftWord.substring(
                            Math.max(0, segment.getLeftWordIndex() - index),
                            segment.getLeftWordIndex()
                    );
                    final int endIndex = Math.min(rightStartIndex + index, leftWord.length());
                    final String postfix = leftWord.substring(rightStartIndex, endIndex);

                    return new AtomicRule(
                            prefix,
                            segment.getLeftWordSubstring(),
                            segment.getRightWordSubstring(),
                            postfix,
                            wordPair.getFrequency()
                    );
                })
                .filter(atomicRule -> isAtomicRuleValid(atomicRule, leftWord));
        if (this.minimumContextLength != null) {
            atomicRuleStream = atomicRuleStream
                    .filter(atomicRule -> atomicRule.getContext().length() >= this.minimumContextLength);
        }
        if (this.maximumNumberOfGeneratedAtomicRules != null) {
            atomicRuleStream = atomicRuleStream
                    .limit(this.maximumNumberOfGeneratedAtomicRules);
        }
        return atomicRuleStream;
    }

    private List<RuleBucket> createRuleBuckets(final Set<ApplicableAtomicRule> applicableAtomicRuleSet) {
        final List<RuleBucket> ruleBuckets = new ArrayList<>();
        ruleBuckets.add(new RuleBucket());

        for (final ApplicableAtomicRule applicableAtomicRule : applicableAtomicRuleSet) {
            boolean foundBucket = false;

            for (final RuleBucket ruleBucket : ruleBuckets) {
                if (ruleBucket.isOverlapping(applicableAtomicRule)) {
                    ruleBucket.add(applicableAtomicRule);
                    foundBucket = true;
                    break;
                }
            }

            if (!foundBucket) {
                final RuleBucket newRuleBucket = new RuleBucket(applicableAtomicRule);
                ruleBuckets.add(newRuleBucket);
            }
        }

        return ruleBuckets;
    }

    private ProbabilisticWord apply(
            final String inputWord,
            final List<ApplicableAtomicRule> applicableAtomicRules) {
        String result = inputWord;

        for (int index = 0; index < applicableAtomicRules.size(); index++) {
            final ApplicableAtomicRule applicableAtomicRule = applicableAtomicRules.get(index);
            final IAtomicRuleComponentAccessor atomicRuleComponentAccessor =
                    applicableAtomicRule.getAtomicRuleComponentAccessor();
            final AtomicRule atomicRule = applicableAtomicRule.getAtomicRule();
            final int leftIndex = applicableAtomicRule.getLeftIndex();
            final int rightIndex = applicableAtomicRule.getRightIndex();
            if (leftIndex > result.length() || leftIndex < 0) {
                return null;
            }
            final String prefix = result.substring(0, leftIndex);
            final String changingSubstring = atomicRuleComponentAccessor.getChangingSubstring(atomicRule);
            final String replacementString = atomicRuleComponentAccessor.getReplacementString(atomicRule);
            if (rightIndex + 1 > result.length() || rightIndex + 1 < 0) {
                return null;
            }
            final String postfix = result.substring(rightIndex + 1);
            result = prefix + replacementString + postfix;

            // Correction of the later atomic rule indices
            final int correction = replacementString.length() - changingSubstring.length();
            for (int correctionIndex = index + 1; correctionIndex < applicableAtomicRules.size(); correctionIndex++) {
                final ApplicableAtomicRule otherApplicableAtomicRule = applicableAtomicRules.get(correctionIndex);

                if (otherApplicableAtomicRule.getLeftIndex() > leftIndex) {
                    final int correctedLeftIndex = otherApplicableAtomicRule.getLeftIndex() + correction;
                    otherApplicableAtomicRule.setLeftIndex(correctedLeftIndex);
                }
                if (otherApplicableAtomicRule.getRightIndex() > rightIndex) {
                    final int correctedRightIndex = otherApplicableAtomicRule.getRightIndex() + correction;
                    otherApplicableAtomicRule.setRightIndex(correctedRightIndex);
                }
            }
        }

        if (result.length() < 2) {
            return null;
        }

        result = result.substring(1, result.length() - 1);
        final double probability = applicableAtomicRules
                .stream()
                .mapToDouble(ApplicableAtomicRule::getFitness)
                .average()
                .orElse(0.0);
        final ProbabilisticWord probabilisticWord = ProbabilisticWord.of(Word.of(result), probability);

        if (LOGGER.isTraceEnabled()) {
            applicableAtomicRules.forEach(applicableAtomicRule -> {
                LOGGER.trace(
                        "[{}] [{}] [{}] Applied atomic rule: {}",
                        applicableAtomicRule.getAtomicRuleComponentAccessor(),
                        this.affixType,
                        inputWord,
                        applicableAtomicRule
                );
            });
        }

        LOGGER.trace(
                "[{}] [{}] [{}]     Output produced: {}",
                applicableAtomicRules.get(0).getAtomicRuleComponentAccessor(),
                this.affixType,
                inputWord,
                probabilisticWord
        );

        return probabilisticWord;
    }

    private List<ProbabilisticWord> filterProbabilisticWords(final List<ProbabilisticWord> probabilisticWords) {
        if (this.maximumResponseProbabilityDifferenceThreshold == null) {
            return probabilisticWords;
        }

        final List<ProbabilisticWord> result = new ArrayList<>(probabilisticWords.size());
        result.add(probabilisticWords.get(0));

        for (int i = 1; i < probabilisticWords.size(); i++) {
            final ProbabilisticWord previousProbabilisticWord = probabilisticWords.get(i - 1);
            final ProbabilisticWord nextProbabilisticWord = probabilisticWords.get(i);
            final double previousProbability = previousProbabilisticWord.getProbability();
            final double nextProbability = nextProbabilisticWord.getProbability();
            final double probabilityDifference = previousProbability - nextProbability;

            if (probabilityDifference >= this.maximumResponseProbabilityDifferenceThreshold) {
                break;
            }

            result.add(nextProbabilisticWord);
        }

        return result;
    }

    private static boolean isAtomicRuleValid(final AtomicRule atomicRule, final String leftWord) {
        return !atomicRule.getContext().isEmpty() &&
                StringUtils.countMatches(leftWord, atomicRule.getContext()) == 1;
    }

