package com.github.szgabsz91.morpher.transformationengines.dictionary.impl.transformationengine;

import com.github.szgabsz91.morpher.core.io.Serializer;
import com.github.szgabsz91.morpher.core.model.AffixType;
import com.github.szgabsz91.morpher.core.model.FrequencyAwareWordPair;
import com.github.szgabsz91.morpher.core.model.Word;
import com.github.szgabsz91.morpher.transformationengines.api.IBidirectionalTransformationEngine;
import com.github.szgabsz91.morpher.transformationengines.api.model.TrainingSet;
import com.github.szgabsz91.morpher.transformationengines.api.model.TransformationEngineResponse;
import com.github.szgabsz91.morpher.transformationengines.dictionary.converters.DictionaryTransformationEngineConverter;
import com.github.szgabsz91.morpher.transformationengines.dictionary.protocolbuffers.DictionaryTransformationEngineMessage;
import com.google.protobuf.Any;
import com.google.protobuf.InvalidProtocolBufferException;

import java.io.IOException;
import java.nio.file.Path;
import java.util.HashMap;
import java.util.Map;
import java.util.Optional;

import static java.util.stream.Collectors.toMap;

/**
 * Dictionary based {@link IBidirectionalTransformationEngine} implementation that uses maps under the hood to store
 * the root and inflected word pairs for forwards and backwards transformation.
 *
 * @author szgabsz91
 */
public class DictionaryTransformationEngine
        implements IBidirectionalTransformationEngine<DictionaryTransformationEngineMessage> {

    private AffixType affixType;
    private Map<Word, Word> forwardsDictionary;
    private Map<Word, Word> backwardsDictionary;

    /**
     * Default constructor that initializes the underlying map of word pairs and the {@link AffixType} that this
     * transformation engine relates to.
     *
     * @param affixType the {@link AffixType} that this transformation engine relates to
     */
    public DictionaryTransformationEngine(final AffixType affixType) {
        this.affixType = affixType;
        this.forwardsDictionary = new HashMap<>();
        this.backwardsDictionary = new HashMap<>();
    }

    /**
     * Returns the {@link AffixType} that this transformation engine relates to.
     * @return the {@link AffixType} that this transformation engine relates to
     */
    @Override
    public AffixType getAffixType() {
        return affixType;
    }

    /**
     * Returns the underlying map of word pairs for forwards transformation.
     * @return the underlying map of word pairs for forwards transformation
     */
    public Map<Word, Word> getForwardsDictionary() {
        return forwardsDictionary;
    }

    /**
     * Returns the underlying map of word pairs for backwards transformation.
     * @return the underlying map of word pairs for backwards transformation
     */
    public Map<Word, Word> getBackwardsDictionary() {
        return backwardsDictionary;
    }

    /**
     * Returns the size of the dictionary.
     * @return the size of the dictionary
     */
    @Override
    public int size() {
        return this.forwardsDictionary.size();
    }

    /**
     * Returns the optional transformation response containing the transformed word forms of the given base form
     * and their probabilities.
     *
     * {@inheritDoc}
     */
    @Override
    public Optional<TransformationEngineResponse> transform(final Word input) {
        final Word result = this.forwardsDictionary.get(input);

        if (result == null) {
            return Optional.empty();
        }

        return Optional.of(TransformationEngineResponse.singleton(result));
    }

    /**
     * Returns the optional backwards transformation response, containing the base forms of the given inflected form
     * and their probabilities.
     *
     * {@inheritDoc}
     */
    @Override
    public Optional<TransformationEngineResponse> transformBack(final Word input) {
        final Word result = this.backwardsDictionary.get(input);

        if (result == null) {
            return Optional.empty();
        }

        return Optional.of(TransformationEngineResponse.singleton(result));
    }

    /**
     * Incorporates the given training set.
     *
     * {@inheritDoc}
     */
    @Override
    public void learn(final TrainingSet trainingSet) {
        trainingSet.getWordPairs()
                .stream()
                .map(FrequencyAwareWordPair::getWordPair)
                .forEach(wordPair -> {
                    this.forwardsDictionary.putIfAbsent(wordPair.getLeftWord(), wordPair.getRightWord());
                    this.backwardsDictionary.putIfAbsent(wordPair.getRightWord(), wordPair.getLeftWord());
                });
    }

    /**
     * Converts the state to a message.
     * @return the message
     */
    @Override
    public DictionaryTransformationEngineMessage toMessage() {
        final DictionaryTransformationEngineConverter dictionaryTransformationEngineConverter =
                new DictionaryTransformationEngineConverter();
        return dictionaryTransformationEngineConverter.convert(this);
    }

    /**
     * Loads the state from the given message.
     * @param message the message
     */
    @Override
    public void fromMessage(final DictionaryTransformationEngineMessage message) {
        final DictionaryTransformationEngineConverter dictionaryTransformationEngineConverter =
                new DictionaryTransformationEngineConverter();
        final DictionaryTransformationEngine dictionaryTransformationEngine =
                dictionaryTransformationEngineConverter.convertBack(message);
        this.affixType = dictionaryTransformationEngine.affixType;
        this.forwardsDictionary = dictionaryTransformationEngine.forwardsDictionary;
        this.backwardsDictionary = this.forwardsDictionary.entrySet()
                .stream()
                .collect(toMap(Map.Entry::getValue, Map.Entry::getKey));
    }

    /**
     * Loads the state from the given message.
     * @param message the message
     * @throws InvalidProtocolBufferException if the provided message is not an
     * {@link DictionaryTransformationEngineMessage}
     */
    @Override
    public void fromMessage(final Any message) throws InvalidProtocolBufferException {
        if (!message.is(DictionaryTransformationEngineMessage.class)) {
            throw new InvalidProtocolBufferException(
                    "The provided message is not an DictionaryTransformationEngineMessage: " + message
            );
        }

        final DictionaryTransformationEngineMessage dictionaryTransformationEngineMessage =
                message.unpack(DictionaryTransformationEngineMessage.class);
        this.fromMessage(dictionaryTransformationEngineMessage);
    }

    /**
     * Saves the state to the given file.
     *
     * {@inheritDoc}
     */
    @Override
    public void saveTo(final Path file) throws IOException {
        final DictionaryTransformationEngineConverter dictionaryTransformationEngineConverter =
                new DictionaryTransformationEngineConverter();
        final Serializer<DictionaryTransformationEngine, DictionaryTransformationEngineMessage> serializer =
                new Serializer<>(dictionaryTransformationEngineConverter, this);
        serializer.serialize(this, file);
    }

    /**
     * Loads the state from the given file.
     *
     * {@inheritDoc}
     */
    @Override
    public void loadFrom(final Path file) throws IOException {
        final DictionaryTransformationEngineConverter dictionaryTransformationEngineConverter =
                new DictionaryTransformationEngineConverter();
        final Serializer<DictionaryTransformationEngine, DictionaryTransformationEngineMessage> serializer =
                new Serializer<>(dictionaryTransformationEngineConverter, this);
        final DictionaryTransformationEngine dictionaryTransformationEngine = serializer.deserialize(file);
        this.affixType = dictionaryTransformationEngine.affixType;
        this.forwardsDictionary = dictionaryTransformationEngine.getForwardsDictionary();
        this.backwardsDictionary = this.forwardsDictionary.entrySet()
                .stream()
                .collect(toMap(Map.Entry::getValue, Map.Entry::getKey));
    }

}
